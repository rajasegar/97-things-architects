<p><div class="chapter" title="Chapter 49. Janus the Architect"><div class="titlepage"><div><div><h1 class="title"><a id="janus_the_architect"/>Chapter 49. Janus the Architect</h1></div></div></div><div class="epigraph"><p>Dave Bartlett is an enthusiastic software professional with more
      than 25 years' experience as a programmer, developer, architect,
      manager, consultant, and instructor. He currently works for clients
      through Commotion Technologies, Inc., a private consulting firm, and
      lectures at Penn State University's Graduate Engineering School in Great
      Valley, Pennsylvania. His main work efforts today are with the Federal
      Reserve Bank of Philadelphia, helping to design and build web, portal,
      and composite applications for use within the Federal Reserve System and
      the United States Treasury.<a class="indexterm" id="IDX-CHP-49-0260"/></p><div class="attribution"><span>—<span class="attribution">David Bartlett</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject49_d1e2944"/><img alt="image with no caption" src="/images/id2705927/OEBPS/httpatomoreillycomsourceoreillyimages251926.png.jpg"/></div></div><p><span class="strong"><strong>IN THE ROMAN WORLD</strong></span>, Janus was the
    god of beginnings and endings, doors and passageways. Janus is usually
    depicted with two heads facing in different directions, a symbol you may
    have seen on coins or in the movies. Janus represents transitions and
    changes in life from past to future, young to old, marriage, births, and
    coming of age.</p><p>For any architect, software or structural, Janus's ability to see
    forward and backward or past to future is a highly regarded skill. An
    architect strives to merge realities with vision; past success with future
    direction; business and management expectations with development
    constraints. Creating these bridges is a major part of being an architect.
    Often an architect may feel she is trying to span chasms while bringing a
    project to completion because of different forces acting on a project—for
    example, ease of access versus security or satisfying present business
    processes while designing for management's future vision. A good architect
    must have those two heads capable of carrying two different ideas or
    thoughts, different goals or visions, to create a product that will
    satisfy the various project stakeholders.</p><p>You should notice that Janus has two heads, not simply two faces.
    This allows Janus to have the extra ears and eyes needed for awareness. An
    excellent IT architect will be a superior listener and evaluator.
    Understanding the reason for a capital expenditure is crucial to
    determining the goals and vision a management team has for the future of
    its organization. Being able to evaluate the technical skills of your
    staff with the design and technology to be used within the project will
    aid in creating the proper training and programming pairs to ensure a
    successful project. Knowing what open source solutions to use in
    combination with common off-the-shelf software can greatly streamline a
    project's timelines and budgets. An excellent architect will be aware of
    many of these disparate pieces of the development process and use them to
    be successful in the project lifecycle.</p><p>There are managers who demand and expect godlike qualities from
    their architects, but that is not the purpose of this comparison. A good
    architect is open to new ideas, tools and designs that progress the
    project, team, or profession; she doesn't want to spend most of her time
    in management meetings or doing all the coding; she should concede to good
    ideas and cultivate an atmosphere for ideas to grow. It is an open mind
    that will succeed in architecture; a mind that can balance the many
    conflicting forces at work on projects. All architects strive to complete
    their projects and ensure the success of their development teams. The best
    architects create systems that stand the test of time because these
    systems are able to be maintained and expanded into the future as the
    organization grows and technology changes. These architects have listened,
    evaluated, and refactored their processes, designs, and methods to ensure
    the success of their work and projects; they have endeavored to ensure
    their products will withstand the transitions and changes that are sure to
    come.</p><p>This is the mindset we should strive for as architects. It is simple
    yet difficult to perform. Like Janus, a software architect needs to be a
    keeper of doors and passageways, spanning the old and the new,
    incorporating creativity with sound engineering to fulfill today's
    requirements while planning to meet tomorrow's expectations.</p></div></p>

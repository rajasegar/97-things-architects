<p><div class="chapter" title="Chapter 27. There Is No 'I' in Architecture"><div class="titlepage"><div><div><h1 class="title"><a id="there_is_no_apostrophy_i_apostrophy"/>Chapter 27. There Is No 'I' in Architecture</h1></div></div></div><div class="epigraph"><p>Dave Quick is the owner, chief architect, janitor, and sole
      employee of Thoughtful Arts. Thoughtful Arts develops off-the-shelf
      software for musicians and provides software design consulting for
      companies who develop music, or arts-oriented software.<a class="indexterm" id="IDX-CHP-27-0114"/></p><div class="attribution"><span>—<span class="attribution">Dave Quick</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject27_d1e1498"/><img alt="image with no caption" src="/images/id2706616/OEBPS/httpatomoreillycomsourceoreillyimages251932.png.jpg"/></div></div><p><span class="strong"><strong>I KNOW, THERE REALLY IS AN 'I' IN
    ARCHITECTURE</strong></span>. But it's not a capital 'I', calling attention to
    itself, dominating discussion. The lowercase character fits neatly within
    the word. It's there only because it fulfills requirements for proper
    spelling and pronunciation.</p><p>How does that relate to us as software architects? Our egos can be
    our own worst enemy. Who hasn't experienced architects who:<a class="indexterm" id="IDX-CHP-27-0115"/></p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>think they understand the requirements better than the
        customers,</p></li><li class="listitem"><p>view developers as resources hired to implement their ideas,
        or</p></li><li class="listitem"><p>get defensive when their ideas are challenged or ignore the
        ideas of others?</p></li></ul></div><p>I suspect any experienced architect has fallen into at least one of
    these traps at some point. I've fallen into all of them and learned
    painful lessons from my mistakes.</p><p>Why does this happen?</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p><span class="emphasis"><em>We've had success</em></span>. Success and experience
        build self-confidence and allow us to become architects. Success leads
        to bigger projects. There is a fine line between self-confidence and
        arrogance. At some point the project is bigger than our personal
        ability. Arrogance sneaks in when we cross that line but don't know it
        yet.</p></li><li class="listitem"><p><span class="emphasis"><em>People respect us</em></span>. Tough design questions
        provide a critical safety net. Our own defensiveness, arrogance, or
        emphasis on our experience can result in missed design
        questions.</p></li><li class="listitem"><p><span class="emphasis"><em>We're human</em></span>. Architects pour themselves
        into each design. Criticism of your creation feels like criticism of
        you. Defensiveness is easy. Learning to stop it is hard. Pride in our
        accomplishments is easy. Recognizing our limitations without conscious
        effort is hard.<a class="indexterm" id="IDX-CHP-27-0116"/></p></li></ul></div><p>How do we avoid it?</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p><span class="emphasis"><em>Requirements don't lie</em></span>. With correct,
        complete requirements, any architecture that meets them is a good one.
        Work closely with the customer to make sure you both understand the
        business value each requirement provides. You don't drive the
        architecture, the requirements do. You do your best to serve their
        needs.</p></li><li class="listitem"><p><span class="emphasis"><em>Focus on the team</em></span>. These are not just
        resources; they are your design collaborators and your safety net.
        People who feel unappreciated usually make a poor safety net. It's the
        teams' architecture, not yours alone. You provide guidance but
        everyone does the heavy lifting together. You need their help as much
        or more than they need yours.</p></li><li class="listitem"><p><span class="emphasis"><em>Check your work</em></span>. The model is not the
        architecture. It is only your understanding of how the architecture
        should work. Work with your team to identify tests that demonstrate
        how the project's architecture supports each requirement.</p></li><li class="listitem"><p><span class="emphasis"><em>Watch yourself</em></span>. Most of us fight our
        natural tendencies to defend our work, focus on our selfish interests,
        and see ourselves as the smartest person in the room. Pressure pushes
        these tendencies to the surface. Consider your interactions for a few
        minutes every day. Did you give everyone's ideas the respect and
        acknowledgment they deserved? Did you react negatively to well-meaning
        input? Do you really understand why someone disagreed with your
        approach?</p></li></ul></div><p>Removing the 'I' from architecture doesn't guarantee success. It
    just removes a common source of failure that's entirely your fault.</p></div></p>

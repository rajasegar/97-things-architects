<p><div class="chapter" title="Chapter 89. Build Systems to Be Zuhanden"><div class="titlepage"><div><div><h1 class="title"><a id="build_systems_to_be_zuhanden"/>Chapter 89. Build Systems to Be Zuhanden</h1></div></div></div><div class="epigraph"><p>After many years as an amateur, Keith Braithwaite was first paid
      to write software in 1996. After that first job, maintaining a compiler
      built with lex and yacc, he progressed first to modelling microwave
      propagation for GSM network planning, then seasonal variations in demand
      for air freight, in C++. A move to consultancy (and Java) introduced him
      to CORBA and then EJB, and then what was called at the time
      "e-commerce." He is currently a principal consultant with Zuhlke and
      manages its Centre of Agile Practice.<a class="indexterm" id="IDX-CHP-89-0310"/></p><div class="attribution"><span>—<span class="attribution">Keith Braithwaite</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject89_d1e4605"/><img alt="image with no caption" src="/images/id2698488/OEBPS/httpatomoreillycomsourceoreillyimages251912.png.jpg"/></div></div><p><span class="strong"><strong>WE BUILD TOOLS</strong></span>. The systems that
    we make have no other reason to exist (nor we to get paid) than to help
    someone, usually someone else, do something.</p><p>Martin Heidegger, an influential German philosopher of the 20th
    century, explored the ways that people experience tools (and more
    generally "equipment") in their lives. People use tools to work toward a
    goal, and the tool is merely a means to an end.</p><p>During successful use, a tool is <span class="emphasis"><em>zuhanden</em></span>
    ("ready-to-hand," having the property of "handiness"). The tool is
    experienced directly; it is used without consideration, without
    theorisation. We grasp the tool and use it to move toward our goal. In
    use, it vanishes! The tool becomes an extension of the user's body and is
    not experienced in its own right. One sign of a tool being zuhanden is
    that it becomes invisible, unfelt, insignificant.<a class="indexterm" id="IDX-CHP-89-0311"/><a class="indexterm" id="IDX-CHP-89-0312"/></p><p>Consider what it feels like to hammer a nail or to write with a pen.
    Think about that immediacy. Think about the way the tool seems to be a
    seamless extension of your body.</p><p>Alternatively, and usually when something has gone wrong with it,
    the user may experience a tool as <span class="emphasis"><em>vorhanden</em></span>
    ("present-at-hand"). The tool is isolated from the goal; it lies before us
    demanding attention. It becomes a topic of investigation in its own right.
    The user is no longer able to proceed toward his goal but must deal first
    with the tool, without it doing anything to move him toward his goal. As
    technologists we tend to experience the systems we build for users as
    vorhanden while we build them, and again when we receive defect reports.
    For us, the tool is quite properly an object of enquiry, of theorising, of
    investigation. It is a thing to be studied.</p><p>However, it is crucial to their success that the users experience
    the tools we build for them as zuhanden. Are your systems architected to
    be invisible in use? Does the user interface fall naturally to hand? Or do
    your systems keep demanding attention, distracting users from their
    goal?</p></div></p>

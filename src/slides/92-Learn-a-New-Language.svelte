<p><div class="chapter" title="Chapter 92. Learn a New Language"><div class="titlepage"><div><div><h1 class="title"><a id="learn_a_new_language"/>Chapter 92. Learn a New Language</h1></div></div></div><div class="epigraph"><p>Burk Hufnagel has been creating positive user experiences since
      1978 and is a lead software architect at LexisNexis.<a class="indexterm" id="IDX-CHP-92-0316"/></p><div class="attribution"><span>—<span class="attribution">Burkhardt Hufnagel</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject92_d1e4721"/><img alt="image with no caption" src="/images/id2727521/OEBPS/httpatomoreillycomsourceoreillyimages252000.png.jpg"/></div></div><p><span class="strong"><strong>TO BE SUCCESSFUL AS AN ARCHITECT</strong></span>,
    you must be able to make yourself understood by people who don't speak
    your native tongue. No, I'm not suggesting you learn Esperanto or even
    Klingon, but you should at least speak basic Business, and Testing. And,
    if you aren't fluent in Programmer, you should make that a top
    priority.</p><p>If you don't see the value in learning other languages, consider the
    following scenario. The business people want a change made to an existing
    system, so they call a meeting with the architect and programmers to
    discuss it. Unfortunately, none of the technical team speaks Business and
    none of the business people speaks Programmer. The meeting will likely go
    something like this:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>A business person talks for a minute about the need for a
        relatively simple enhancement to an existing product, and explains how
        making the change will enable the sales team to increase both market
        and mind share.</p></li><li class="listitem"><p>While the business person is still speaking, the architect
        starts sketching some kind of occult symbols on a notepad and enters
        into quiet argument with the one of the programmers in their strange
        multisyllabic tongue.</p></li><li class="listitem"><p>Eventually the business person finishes and looks expectantly at
        the architect.</p></li><li class="listitem"><p>After the whispered argument completes, the architect walks to
        the whiteboard and begins drawing several complex diagrams that are
        supposed to represent multiple views of the existing system while
        explaining (in complex technical terms) why the requested enhancement
        is virtually impossible without major changes and may actually require
        a complete redesign/rewrite of the entire system.</p></li><li class="listitem"><p>The business people (who understood little of the diagram and
        less of the explanation) are openly stunned and find it hard to
        believe that something so simple would require such massive changes.
        They begin to wonder if the architect is serious or just making things
        up to avoid making the change.</p></li><li class="listitem"><p>Meanwhile, the architect and programmers are just as surprised
        that the business people don't see how the "minor" change will require
        major modifications to the core system functionality.</p></li></ul></div><p>And therein lies the problem. Neither group understands how the
    other thinks or what half of the words they use means. This leads to
    mistrust and miscommunication. It's a basic psychological principle that
    people are more comfortable with those who are similar to them than those
    who are different from them.</p><p>Imagine how the aforementioned scenario might change if the
    architect were able to explain the issues to the business folk in terms
    they understand and relay the business issues to the programmers in terms
    they understand. Instead of surprise and mistrust, the result would be
    agreement and approval.</p><p>I'm not saying that learning multiple languages will cure all your
    problems, but it will help prevent the miscommunications and
    misunderstandings that lead to problems.</p><p>For those of you who decide this makes sense, I wish you success on
    your journey. Or, as the Klingons say, <span class="emphasis"><em>Qapla</em></span>!</p></div></p>

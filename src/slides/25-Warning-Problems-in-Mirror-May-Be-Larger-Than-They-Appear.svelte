<p><div class="chapter" title="Chapter 25. Warning: Problems in Mirror May Be Larger Than They Appear"><div class="titlepage"><div><div><h1 class="title"><a id="warning_colon_problems_in_mirror_may_be"/>Chapter 25. Warning: Problems in Mirror May Be Larger Than They Appear</h1></div></div></div><div class="epigraph"><p>Dave Quick is the owner, chief architect, janitor, and sole
      employee of Thoughtful Arts. Thoughtful Arts develops off-the-shelf
      software for musicians and provides software design consulting for
      companies who develop music, or arts-oriented software.<a class="indexterm" id="IDX-CHP-25-0099"/></p><div class="attribution"><span>—<span class="attribution">Dave Quick</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject25_d1e1350"/><img alt="image with no caption" src="/images/id2706616/OEBPS/httpatomoreillycomsourceoreillyimages251932.png.jpg"/></div></div><p><span class="strong"><strong>I'VE WORKED ON HUNDREDS OF SOFTWARE
    PROJECTS</strong></span>. Every one had issues that caused more problems than
    the team expected. Often, a small part of the team identified the issue
    early on and the majority dismissed or ignored it because they didn't
    understand how important it really was until it was too late.</p><p>The forces at work include:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>Issues that seemed trivial early in the project become critical
        after it is too late to fix them. While the boiling frog experiment
        may be folklore, it's a useful analogy for what happens in many
        projects.</p></li><li class="listitem"><p>Individuals often face resistance when the rest of the team does
        not share their experience or knowledge. Overcoming this resistance
        requires unusual courage, confidence, and persuasiveness. It rarely
        happens, even with highly paid, experienced consultants specifically
        hired to help avoid such problems.</p></li><li class="listitem"><p>Most software developers are optimists. Painful experience
        teaches us to temper our optimism, but without specific experience we
        tend toward optimism. Natural pessimists on development teams are
        often unpopular, even if they are consistently right. Few people will
        risk this reputation and take a stand against the majority without a
        very solid case. Most of us have had the "This makes me uncomfortable,
        but I can't explain why" feeling, but sharing it rarely wins any
        arguments.</p></li><li class="listitem"><p>Every team member has a different view of what is more or less
        important. Their concerns are often focused on their personal
        responsibilities, not the project's goals.</p></li><li class="listitem"><p>We all have blind spots, shortcomings that are difficult for us
        to recognize or to accept.</p></li></ul></div><p>Some possible strategies to counteract these forces could
    include:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>Establish an organized approach to managing risks. One simple
        approach is to track risks the same way you track bugs. Anyone can
        identify a risk, and each risk is tracked until it is no longer a
        risk. Risks are prioritized and reviewed when their status changes or
        when there is new information. This helps remove emotion from the
        discussion and makes it easier to remember to re-evaluate risks
        periodically.<a class="indexterm" id="IDX-CHP-25-0100"/></p></li><li class="listitem"><p>When going against the majority, look for ways to help the rest
        of the team understand more easily. Encourage any team you're on to
        recognize the value in dissenting opinions and look for neutral ways
        to discuss them.</p></li><li class="listitem"><p>"Bad smells" are worth recognizing. If the facts aren't there
        yet, look for the simplest tests that would provide the facts.</p></li><li class="listitem"><p>Constantly test your understanding against the team and the
        customer. Tools such as a prioritized list of user stories can help,
        but are no substitute for regular communications with the customer and
        an open mind.</p></li><li class="listitem"><p>Blind spots are, by definition, hard to recognize. People you
        trust to tell you the hard truth when you need it are a precious
        resource.</p></li></ul></div></div></p>

<p><div class="chapter" title="Chapter 39. Heterogeneity Wins"><div class="titlepage"><div><div><h1 class="title"><a id="heterogeneity_wins"/>Chapter 39. Heterogeneity Wins</h1></div></div></div><div class="epigraph"><p>Edward Garson has been passionate about technology since learning
      to program in Logo on the Apple II. He currently works as a software
      architect in the Center for Agile Practices at Zuhlke Engineering, a
      leading Swiss-based technology firm.<a class="indexterm" id="IDX-CHP-39-0230"/></p><div class="attribution"><span>—<span class="attribution">Edward Garson</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject39_d1e2348"/><img alt="image with no caption" src="/images/id2713913/OEBPS/httpatomoreillycomsourceoreillyimages251942.png.jpg"/></div></div><p><span class="strong"><strong>THE NATURAL EVOLUTION OF COMPUTER
    TECHNOLOGY</strong></span> has brought about important changes to the tools
    that architects can use to build software systems. These changes have
    brought about a resurgence of interest in polyglot programming, which
    refers to the use of more than one core language in the provision of a
    software system.<a class="indexterm" id="IDX-CHP-39-0231"/></p><p>Polyglot programming is not a new concept: one prominent example
    from the past is frontend Visual Basic clients supported by COM objects
    authored in C++ on the backend. Fundamentally speaking, this architecture
    leveraged what those languages were good at in their heyday.</p><p>So, what changes took place to fuel this renewed interest in
    polyglot programming?</p><p>The change is that technical standards, together with
    ever-increasing bandwidth and computing resources, conspired to make
    text-based protocols viable; gone are the days of arcane binary protocols
    as a prerequisite to efficient distributed systems. Text-based remote
    interoperability largely began with XML/SOAP-based web services and
    continues to evolve with RESTful architectural styles and other supporting
    (but no less important) protocols such as Atom and XMPP.</p><p>This new breed of technologies creates far broader opportunities for
    heterogeneous development than ever before, simply because the payload is
    formatted text, which is universally generated and consumed. Heterogeneous
    development affords using the right tool for the job, and text-based
    interop has blown the doors off what was previously possible.</p><p>Architects can now combine specific, powerful tools that move the
    yardstick from previously being able to employ the right language to now
    being able to employ the right paradigm. Programming languages support
    different paradigms, in that some are object-oriented, while others are
    functional or excel at concurrent programming. Some of these paradigms are
    a perfect match for particular problems or domains, while others are a
    poor fit. Today, however, it is possible to "mash up" some rather
    unconventional and seemingly dissonant tools into elegant solutions rather
    more easily than in the past.</p><p>The effect of this change has begun, and manifests itself as a
    combinatorial increase in the architectural topology of modern software
    systems. This is not just a reflection of their inherent diversity, but a
    testament to new possibilities.</p><p>While choice is not always a good thing, it is "less worse" than the
    alternative in the context of modern software architecture. As an
    industry, we are faced with very serious problems<sup>[<a class="footnote" href="#ftn.CHP-39-FN-1" id="CHP-39-FN-1">4</a>]</sup> and we need all the interoperability we can get,
    particularly as the incumbent platforms are not well equipped to resolve
    them.<sup>[<a class="footnote" href="#ftn.CHP-39-FN-2" id="CHP-39-FN-2">5</a>]</sup><a class="indexterm" id="IDX-CHP-39-0232"/></p><p>Your job as architect has become even more challenging because
    technology silos are crumbling in the face of new possibilities: embrace
    this, think outside the stack, and leverage the new diversity:
    heterogeneity wins.</p><div class="footnotes"><br/><hr/><div class="footnote"><p><sup>[<a class="para" href="#CHP-39-FN-1" id="ftn.CHP-39-FN-1">4</a>] </sup>The impending multicore era may well prove to be the most
        significant problem yet faced by the software development
        community.</p></div><div class="footnote"><p><sup>[<a class="para" href="#CHP-39-FN-2" id="ftn.CHP-39-FN-2">5</a>] </sup>See "The Free Lunch is Over," by Herb Sutter (<a class="ulink" href="http://www.gotw.ca/publications/concurrency-ddj.htm">http://www.gotw.ca/publications/concurrency-ddj.htm</a>).</p></div></div></div></p>

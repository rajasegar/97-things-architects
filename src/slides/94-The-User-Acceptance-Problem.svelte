      Portal, and Lotus Domino projects.<a class="indexterm" id="IDX-CHP-94-0325"/></p><div class="attribution"><span>—<span class="attribution">Norman Carnovale</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject94_d1e4843"/><img alt="image with no caption" src="/images/id2706051/OEBPS/httpatomoreillycomsourceoreillyimages251928.png.jpg"/></div></div><p><span class="strong"><strong>PEOPLE AREN'T ALWAYS HAPPY</strong></span> about
    new systems or major upgrades. This can pose a threat to the successful
    completion of a project.</p><p>It's not uncommon for people to disagree with the decision to
    implement a new system—especially at the beginning. This should be
    expected, and the reasons noted. However, initial reactions to a new
    system are less of a concern than a sustained negative reaction.</p><p>Your goal as an architect is to be aware of and measure the threat
    of acceptance problems and work toward mitigating those threats. To do
    this you have to be cognizant of them and consider the reasons for them.
    Some of the more common reasons are:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>People may have concerns about the need for a new system (and
        subsequent retirement of an old system). This can also include fear of
        losing functionality or losing influence or power when roles
        change.</p></li><li class="listitem"><p>People fear new (unproven) technology.</p></li><li class="listitem"><p>People have cost/budget concerns.</p></li><li class="listitem"><p>People simply do not like change.</p></li></ul></div><p>Each of these reasons requires different possible solutions, some of
    which you can address and others you can't. You have to recognize the
    difference and deal quickly with those that you can. Start early having
    discussions with your end users about the new system and its real and
    perceived benefits and disadvantages. The most effective long-term
    solution is to use the design of the system itself to address the
    concerns. Other effective solutions include training, scheduled system
    demonstrations (early in the project lifecycle), and sharing knowledge of
    what users will get with a new system.</p><p>A "project champion" can help avoid user acceptance problems.
    Ideally this should be a person that represents the user group or
    stakeholders. This person sometimes has to be convinced himself. If there
    is none, then push for one from the very beginning. Once you've recruited
    a project champion, give him your assistance in every way you can.</p></div></p>

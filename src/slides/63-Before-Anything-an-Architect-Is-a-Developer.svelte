<p><div class="chapter" title="Chapter 63. Before Anything, an Architect Is a Developer"><div class="titlepage"><div><div><h1 class="title"><a id="before_anything_comma_an_architect_is_a"/>Chapter 63. Before Anything, an Architect Is a Developer</h1></div></div></div><div class="epigraph"><p>Mike Brown is a lead software engineer for Software Engineering
      Professionals, Inc. (<a class="ulink" href="http://www.sep.com">http://www.sep.com</a>). He has
      13 years of experience in IT, including 8 years' experience developing
      enterprise solutions in a wide range of vertical markets. He is a
      founder of the Indianapolis Alt.NET user group, a charter member of the
      WPF Disciples, and organizer of the upcoming Indy Arc professional user
      group.<a class="indexterm" id="IDX-CHP-63-0282"/></p><div class="attribution"><span>—<span class="attribution">Mike Brown</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject63_d1e3542"/><img alt="image with no caption" src="/images/id2717536/OEBPS/httpatomoreillycomsourceoreillyimages251970.png.jpg"/></div></div><p><span class="strong"><strong>HAVE YOU HEARD OF A JUDGE WHO WASN'T A
    LAWYER</strong></span>, or a chief of surgery who wasn't a surgeon? Even after
    they get to what some would call the pinnacles of their career, the people
    holding these occupations are still expected to continue learning the new
    developments within their respective fields. As software architects, we
    should be held to the same standards.</p><p>No matter how well designed a solution is, one of the most important
    factors for determining the success of an implementation is getting the
    developers to sign on to the game plan. The quickest way to get the
    developers to sign on is to gain their respect and trust. We all know the
    quickest way to gain a developer's trust: <span class="emphasis"><em>your code is your
    currency</em></span>. If you can show your developers that you're not just
    some pie-in-the-sky daydreamer who can't code his way out of a paper bag,
    you'll hear less grumbling about the hoops you're "making" them jump
    through to get data to show on the page when "I can get it done in less
    time by just binding a dataset to a grid."</p><p>Even though I'm not required to as part of my job, I will frequently
    pick up some of the more intricate tasks. This serves two purposes: first
    it's fun and helps me to keep my development skills sharp; second, it
    helps me demonstrate to my developers that I'm not just blowing smoke
    where the sun doesn't shine.</p><p>As an architect, your primary goal should be to create a solution
    that is feasible, maintainable, and of course addresses the issue at hand.
    Part of knowing what is feasible in a solution is having knowledge of the
    effort involved in developing the elements of the solution. Therefore, I
    propose that <span class="emphasis"><em>if you design it, you should be able to code
    it</em></span>.</p></div></p>

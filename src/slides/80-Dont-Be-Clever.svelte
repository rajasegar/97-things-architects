      multibillion-dollar national retail company, where he is currently
      focused on designing and implementing its service-oriented architecture.
      He is the author of the upcoming <span class="emphasis"><em>Java SOA Cookbook</em></span>
      from O'Reilly.<a class="indexterm" id="IDX-CHP-80-0294"/></p><div class="attribution"><span>—<span class="attribution">Eben Hewitt</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject80_d1e4236"/><img alt="image with no caption" src="/images/id2719601/OEBPS/httpatomoreillycomsourceoreillyimages251994.png.jpg"/></div></div><p><span class="strong"><strong>GENERAL INTELLIGENCE,
    RESOURCEFULNESS</strong></span>, thoughtfulness, a breadth and depth of
    knowledge, and an affinity for precision are laudable qualities in anyone,
    and particularly prized in architects.</p><p>Cleverness, however, carries a certain additional connotation. It
    implies an ability to quickly conceive of a solution that may get you out
    of a jam, but that ultimately rests on a gimmick, a shell game, or a
    switcharoo. We remember clever debaters from high school—always able to
    play semantics or work the logical fallacies to win the point.</p><p>Clever software is expensive, hard to maintain, and brittle. Don't
    be clever. Be as dumb as you possibly can and still create the appropriate
    design. The appropriate design will never be clever. If cleverness appears
    absolutely required, the problem is incorrectly framed; reset the problem.
    Reframe it until you can be dumb again. Work in rough chalk sketches; stay
    general. Let go of the flavor of the day. It takes a smart architect to be
    dumb.<a class="indexterm" id="IDX-CHP-80-0295"/></p><p>It is our cleverness that allows us to trick software into working.
    Don't be the attorney who gets your software off on a technicality. We are
    not Rube Goldberg. We are not MacGyver, ever ready to pull some
    complicated design out of our hats having been allowed only a paper clip,
    a firecracker, and a piece of chewing gum. Empty your head and approach
    the problem without your extensive knowledge of closures and generics and
    how to manipulate object graduation in the heap. Sometimes of course, such
    stuff is exactly what we need. But less often than we might think.</p><p>More developers can implement and maintain dumb solutions. In dumb
    solutions, each component can do only one thing. They will take less time
    to create, and less time to change later. They inherit optimizations from
    the building blocks you're using. They emerge from the page as a living
    process, and you can feel their elegance and simplicity. Clever designs
    will stay stubbornly rooted; their details are too embroiled in the
    overall picture. They crumble if you touch them.</p></div></p>

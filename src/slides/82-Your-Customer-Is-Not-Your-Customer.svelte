<p><div class="chapter" title="Chapter 82. Your Customer Is Not Your Customer"><div class="titlepage"><div><div><h1 class="title"><a id="your_customer_is_not_your_customer"/>Chapter 82. Your Customer Is Not Your Customer</h1></div></div></div><div class="epigraph"><p>Eben Hewitt is a principal on the architecture team at a
      multibillion-dollar national retail company, where he is currently
      focused on designing and implementing its service-oriented architecture.
      He is the author of the upcoming <span class="emphasis"><em>Java SOA Cookbook</em></span>
      from O'Reilly.<a class="indexterm" id="IDX-CHP-82-0297"/></p><div class="attribution"><span>—<span class="attribution">Eben Hewitt</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject82_d1e4306"/><img alt="image with no caption" src="/images/id2719601/OEBPS/httpatomoreillycomsourceoreillyimages251994.png.jpg"/></div></div><p><span class="strong"><strong>AS YOU WORK IN REQUIREMENTS
    MEETINGS</strong></span> to design software, pretend that your customer is not
    your customer. It turns out that this is a very easy thing to do, because
    it is true.</p><p>Your customer is not your customer. Your customer's customer is your
    customer. If your customer's customer wins, your customer wins. Which
    means you win.</p><p>If you're writing an e-commerce application, take care of the things
    that you know people who will shop at that site will need. They'll need
    transport security. They'll need encryption of stored data. Your customer
    may not mention these requirements. If you know that your customer is
    leaving out things your customer's customer will need, address them, and
    communicate why.<a class="indexterm" id="IDX-CHP-82-0298"/><a class="indexterm" id="IDX-CHP-82-0299"/></p><p>If your customer willingly and knowingly doesn't care about certain
    important things that your customer's customer cares about—as happens from
    time to time—consider stepping away from the project. Just because Sally
    Customer doesn't want to pay for SSL every year and wants to store credit
    cards in plain text because it costs less to build, it's not OK to simply
    agree. You're killing your customer's customer when you agree to do work
    you know is a bad idea.</p><p>Requirements-gathering meetings are not implementation meetings.
    Forbid the customer's use of implementation-specific terms unless it's an
    absolute or well-understood problem. Allow your customer to express only
    the Platonic ideal, his concept and goals, rather than dictating a
    solution or even using technical terms.</p><p>So, how do you maintain such discipline in these meetings, which can
    be deceptively difficult? Remember to care for your customer's customer.
    Remember that while your customer is writing your check, you must be clear
    that you need to honor best practices, so that you can make what the
    customer really needs, not just what they say they need. Of course, this
    takes lots of discussion, and clarity as to exactly what you're doing and
    why.</p><p>Perhaps, as with so many things in life, this is best clarified by a
    poem. In 1649, Richard Lovelace wrote "To Lucasta, on Going to the Wars."
    It ends with the line: "I could not love thee, dear, so much,/Loved I not
    honor more."</p><p>We cannot love our customers so much, love we not their customers
    more.</p></div></p>

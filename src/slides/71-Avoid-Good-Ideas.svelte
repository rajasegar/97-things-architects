<p><div class="chapter" title="Chapter 71. Avoid &quot;Good Ideas&quot;"><div class="titlepage"><div><div><h1 class="title"><a id="avoid_quotation_mark_good_ideas_quot"/>Chapter 71. Avoid "Good Ideas"</h1></div></div></div><div class="epigraph"><p>Greg Nyberg is currently an independent J2EE computer consultant
      with 18 years' experience designing, building, testing, and deploying
      large, high-volume, transactional applications such as reservation
      systems, call centers, and consumer websites. He is the author of the
      WebLogic companion workbook for <span class="emphasis"><em>Enterprise
      JavaBeans</em></span>, Third Edition, (O'Reilly), and the lead author of
      the book <span class="emphasis"><em>Mastering WebLogic Server</em></span>
      (Wiley).<a class="indexterm" id="IDX-CHP-71-0286"/></p><div class="attribution"><span>—<span class="attribution">Greg Nyberg</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject71_d1e3863"/><img alt="image with no caption" src="/images/id2718349/OEBPS/httpatomoreillycomsourceoreillyimages251982.png.jpg"/></div></div><p><span class="strong"><strong>GOOD IDEAS KILL PROJECTS</strong></span>.
    Sometimes it's a quick death, but often it's a slow, lingering death
    caused by missed milestones and a spiraling bug count.</p><p>You know the kinds of good ideas I'm talking about: tempting,
    no-brainer, innocent-looking, couldn't-possibly-hurt-to-try sorts of
    ideas. They usually occur to someone on the team about halfway through a
    project when everything seems to be going fine. Stories and tasks are
    getting knocked off at a good pace, initial testing is going well, and the
    rollout date looks solid. Life is good.</p><p>Someone has a "good idea," you acquiesce, and suddenly you are
    refitting a new version of Hibernate into your project to take advantage
    of the latest features, or implementing AJAX in some of your web pages
    because the developer showed the user how cool it is, or even revisiting
    the database design to utilize XML features of the RDBMS. You tell the
    project manager you need a few weeks to implement this "good idea," but it
    ends up impacting more code than originally anticipated, and your schedule
    starts to slip. Plus, by letting in the first "good idea," you've allowed
    the proverbial camel's nose in the tent, and soon the good ideas are
    coming out of the woodwork and it becomes harder to say no (and the camel
    is soon sleeping in your bed).</p><p>The really insidious thing about "good ideas" is that they are
    "good." Everyone can recognize and reject "bad" ideas out of hand—it's the
    good ones that slip through and cause trouble with scope, complexity, and
    sheer wasted effort incorporating something into the application that
    isn't necessary to meet the business need.</p><p>Here are some key phrases to look for:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>"Wouldn't it be cool if...." Really, any sentence with the word
        "cool" in it is a danger signal.</p></li><li class="listitem"><p>"Hey, they just released version XXX of the YYY framework. We
        ought to upgrade!"</p></li><li class="listitem"><p>"You know, we really should refactor XXX as long as we are
        working on ZZZ...."</p></li><li class="listitem"><p>"That XXX technology is really powerful! Maybe we could use it
        on...."</p></li><li class="listitem"><p>"Hey, &lt;<span class="emphasis"><em>yournamehere</em></span>&gt;, I've been
        thinking about the design and I have an idea!"</p></li></ul></div><p>OK, OK, maybe I'm being a bit too cynical with that last one. But
    keep watching out for "good ideas" that can kill your project.</p></div></p>

<p><div class="chapter" title="Chapter 78. It Takes Diligence"><div class="titlepage"><div><div><h1 class="title"><a id="it_takes_diligence"/>Chapter 78. It Takes Diligence</h1></div></div></div><div class="epigraph"><p>Brian Hart is an executive consultant with CGI, a leading IT and
      business process services provider. Brian is involved in the
      architecture and design of J2EE applications primarily in the state and
      local government sector. He has been involved in the software industry
      since 1997.</p><div class="attribution"><span>—<span class="attribution">Brian Hart</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject78_d1e4134"/><img alt="image with no caption" src="/images/id2719335/OEBPS/httpatomoreillycomsourceoreillyimages251990.png.jpg"/></div></div><p><span class="strong"><strong>AN ARCHITECT'S JOB IS OFTEN
    PORTRAYED</strong></span> as an activity focused on ingenuity and problem
    solving. Ingenuity is a key trait of successful architects. However, an
    equally important characteristic of the activities of a successful
    architect is diligence. Diligence can manifest itself in many ways, but
    ultimately it is an exercise in perseverance and paying the right amount
    of attention to each task and each architectural goal of the
    system.<a class="indexterm" id="IDX-CHP-78-0291"/><a class="indexterm" id="IDX-CHP-78-0292"/></p><p>Diligence goes hand in hand with the mundane. Successful
    architecture practices are in many ways mundane. Effective architects
    often follow mundane daily and weekly checklists to remind them of that
    which they already know academically, but fail to practice by habit.
    Without such mundane checklists and reminders, architects can quickly fall
    into software time, in which no measurable progress is achieved because a
    lack of diligence allowed the architecture to meander and violate known
    academic principles. It is important to realize in these retrospectives of
    failed projects that in most cases it isn't incompetence that drove
    failure, but rather the lack of both diligence and a sense of
    urgency.</p><p>Diligence also requires an architect to succeed at the deceptively
    simple task of making and keeping commitments. These commitments are often
    disparate and can encompass a wide range of constraints and expectations.
    Examples include:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>Embracing the budget and time constraints of the customer</p></li><li class="listitem"><p>Performing all the work that makes the architect effective, not
        just the work the architect enjoys</p></li><li class="listitem"><p>Committing to the process/methodology</p></li><li class="listitem"><p>Accepting responsibility</p></li></ul></div><p>Atul Gawande, in his terrific book <span class="emphasis"><em>Better: A Surgeon's
    Notes on Performance</em></span> (Metropolitan Books), speaks of diligence
    in the medical community:</p><div class="blockquote"><blockquote class="blockquote"><p><span class="emphasis"><em>True success in medicine is not easy. It requires will,
      attention to detail, and creativity. But the lesson I took from India
      was that it is possible anywhere and by anyone. I can imagine few places
      with more difficult conditions. Yet astonishing success could be
      found...what I saw was: Better is possible. It does not take genius. It
      takes diligence. It takes moral clarity. It takes ingenuity. And above
      all, it takes a willingness to try</em></span>.</p></blockquote></div></div></p>

    all of these characters, and ensure that the architecture has roles for
    all of them. An architecture designed only for one will attract only that
    one character to the project, and even with the best dwarves, or elves, or
    wizards, the team will be severely limited in its reach if it can only
    approach problems in one way.</p><p>A good king will lead all types through a quest, and help them work
    together to complete it. Without the quest, there is no vision for the
    team, and it ultimately becomes a partisan mess. Without all the
    characters, the team can only solve one class of problem, and is stopped
    at the first barrier impassable to that solution.</p><p>The architect creates the quest with all the characters in mind. The
    architecture then becomes a guide for finding tasks for the different
    characters to perform while learning about one another. When a project
    encounters difficulty, the team will already know how to approach solving
    it because the architecture gave them the opportunities to grow into a
    team.</p></div></p>

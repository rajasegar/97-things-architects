<p><div class="chapter" title="Chapter 34. &quot;Software Architect&quot; Has Only Lowercase a's; Deal with It"><div class="titlepage"><div><div><h1 class="title"><a id="quotation_mark_software_architect_quot"/>Chapter 34. "Software Architect" Has Only Lowercase a's; Deal with It</h1></div></div></div><div class="epigraph"><p>Barry Hawkins has played various roles in his 13 years in the
      software industry, from lone developer to team lead to Agile coach and
      mentor. Barry is one of the few native Atlantans, currently specializing
      in coaching and mentoring for Agile software development and
      domain-driven design.</p><div class="attribution"><span>—<span class="attribution">Barry Hawkins</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject34_d1e2124"/><img alt="image with no caption" src="/images/id2713246/OEBPS/httpatomoreillycomsourceoreillyimages251940.png.jpg"/></div></div><p><span class="strong"><strong>A DISAPPOINTING TREND</strong></span> has been in
    bloom for some time now within software development: the attempt to
    professionalize the practice of software architecture as one on par with
    the classical school of Architecture. This seems to stem from some need to
    further legitimize one's accomplishment beyond acknowledgment among one's
    peers and employer. By comparison, Architecture itself was not
    professionalized until the late 19th century, at least a few millennia
    after the practice had been around. It would be no great stretch to say
    that some software architects seem a bit eager by comparison.</p><p>Software architecture is a craft, and it certainly takes practice
    and discipline to achieve success in the field. That said, software
    development is still a relatively nascent endeavor. We don't even know
    enough about this practice to adequately professionalize it. Despite its
    youth, software development's product has become a highly valued tool, and
    as such, the accomplished individuals (as well as those who wish to be
    seen as accomplished) have enjoyed levels of compensation on par with the
    leading professional fields, including medicine, accounting, and
    law.</p><p>Practitioners of software development enjoy considerable
    compensation for work that is highly creative and exploratory. The fruits
    of our labors have been used to accomplish many significant milestones,
    some that benefit all of mankind. The barriers to entry are largely one's
    own merit and opportunity; the fully professionalized fields undergo
    programs of study and internship that dwarf our own. Dwell on that for a
    moment and ponder how much cause we have to be content, and just how brash
    it is to insist that software architect be considered a title that sits as
    peer to Lawyer, Doctor, and Architect.</p><p>The title of software architect has only lowercase
    <span class="emphasis"><em>a</em></span>'s; deal with it.</p></div></p>

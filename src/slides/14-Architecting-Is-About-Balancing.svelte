        Requirements</h2></div></div></div><p><span class="strong"><strong>WHEN WE THINK OF ARCHITECTING
        SOFTWARE</strong></span>, we tend to think first of classical technical
        activities, like modularizing systems, defining interfaces, allocating
        responsibility, applying patterns, and optimizing performance.
        Architects also need to consider security, usability, supportability,
        release management, and deployment options, among other things. But
        these technical and procedural issues must be balanced with the needs
        of stakeholders and their interests. Taking a "stakeholders and
        interests" approach in requirements analysis is an excellent way to
        ensure completeness of requirements specifications for the software
        being developed.<a class="indexterm" id="IDX-CHP-14-0061"/><a class="indexterm" id="IDX-CHP-14-0064"/><a class="indexterm" id="IDX-CHP-14-0062"/><a class="indexterm" id="IDX-CHP-14-0065"/><a class="indexterm" id="IDX-CHP-14-0063"/><a class="indexterm" id="IDX-CHP-14-0066"/><a class="indexterm" id="IDX-CHP-14-0067"/><a class="indexterm" id="IDX-CHP-14-0068"/><a class="indexterm" id="IDX-CHP-14-0069"/><a class="indexterm" id="IDX-CHP-14-0070"/><a class="indexterm" id="IDX-CHP-14-0071"/><a class="indexterm" id="IDX-CHP-14-0072"/><a class="indexterm" id="IDX-CHP-14-0073"/><a class="indexterm" id="IDX-CHP-14-0074"/><a class="indexterm" id="IDX-CHP-14-0075"/><a class="indexterm" id="IDX-CHP-14-0076"/><a class="indexterm" id="IDX-CHP-14-0077"/><a class="indexterm" id="IDX-CHP-14-0078"/><a class="indexterm" id="IDX-CHP-14-0079"/><a class="indexterm" id="IDX-CHP-14-0080"/><a class="indexterm" id="IDX-CHP-14-0081"/></p><p>Analyzing the stakeholders and their interests in the process by
        which an organization develops software, and in the organization
        itself, reveals the ultimate set of priorities bearing on a software
        architect. Software architecting is about balancing this set of
        priorities, over the short and long term, in a way that is appropriate
        to the context at hand.</p><p>Consider, for example, the engineering department of a
        software-as-a-service business. The business likely has certain
        priorities, such as meeting contractual obligations, generating
        revenue, ensuring customer referenceability, containing costs, and
        creating valuable technology assets. These business priorities may
        translate to departmental priorities like ensuring the functionality,
        correctness, and "quality attributes" (i.e., "-ilities") of the
        software being developed, as well as ensuring the productivity of the
        development team, the sustainability and auditability of development
        operations, and the adaptability and longevity of the software
        products.</p><p>It is the architect's job to not only create functional, quality
        software for users, but also to do so while balancing the other
        departmental priorities with the cost-containment interests of the
        business's CEO, with the ease-of-administration interests of the
        operations staff, with the ease-of-learning and ease-of-maintenance
        interests of future programming staff, and with the best practices of
        the software architect's profession.</p><p>The architect may choose to consciously tilt the balance in
        favor of one priority in the short term, but had better maintain a
        proper balance over the long term in order to truly do the job well.
        And the balance that is struck needs to be appropriate to the context
        at hand, in consideration of factors such as the expected lifespan of
        the software, the criticality of the software to the business, and the
        technological and financial culture of the organization.</p><p>In summary, software architecting is about more than just the
        classical technical activities; it is about balancing technical
        requirements with the business requirements of stakeholders in the
        project.</p></div></div></div></p>

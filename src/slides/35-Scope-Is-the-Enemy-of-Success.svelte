<p><div class="chapter" title="Chapter 35. Scope Is the Enemy of Success"><div class="titlepage"><div><div><h1 class="title"><a id="scope_is_the_enemy_of_success"/>Chapter 35. Scope Is the Enemy of Success</h1></div></div></div><div class="epigraph"><p>Dave Quick is the owner, chief architect, janitor, and sole
      employee of Thoughtful Arts. Thoughtful Arts develops off-the-shelf
      software for musicians and provides software design consulting for
      companies who develop music, or arts-oriented software.<a class="indexterm" id="IDX-CHP-35-0223"/></p><div class="attribution"><span>—<span class="attribution">Dave Quick</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject35_d1e2156"/><img alt="image with no caption" src="/images/id2706616/OEBPS/httpatomoreillycomsourceoreillyimages251932.png.jpg"/></div></div><p><span class="strong"><strong>SCOPE REFERS TO A PROJECT'S SIZE</strong></span>.
    How much time, effort, and resources? What functionality at what level of
    quality? How difficult to deliver? How much risk? What constraints exist?
    The answers define a project's scope. Software architects love the
    challenge of big, complicated projects. The potential rewards can even
    tempt people to artificially expand a project's scope to increase its
    apparent importance. Expanding scope is the enemy of success because the
    probability of failure grows faster than expected. Doubling a project's
    scope often increases its probability of failure by an order of
    magnitude.<a class="indexterm" id="IDX-CHP-35-0224"/></p><p>Why does it work this way? Consider some examples:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>Intuition tells us to double our time or resources to do twice
        as much work. History<sup>[<a class="footnote" href="#ftn.CHP-35-FN-1" id="CHP-35-FN-1">2</a>]</sup> says impacts are not as linear as intuition suggests.
        For example, a four-person team will expend more than twice the
        communication effort as a team of two.</p></li><li class="listitem"><p>Estimation is far from an exact science. Who hasn't seen
        features that were much harder to implement than expected?</p></li></ul></div><p>Of course, some projects aren't worth doing without some built-in
    size and complexity. While a text editor without the ability to enter text
    might be easy to build, it wouldn't be a text editor. So, what strategies
    can help to reduce or manage scope in real-world projects?</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p><span class="emphasis"><em>Understand the real needs</em></span>. The capabilities
        a project must deliver are a set of requirements. Requirements define
        functionality or qualities of functionality. Question any requirements
        not explained in terms of measurable value to the customer. If it has
        no effect on the company's bottom line, why is it a
        requirement?<a class="indexterm" id="IDX-CHP-35-0225"/></p></li><li class="listitem"><p><span class="emphasis"><em>Divide and conquer</em></span>. Look for opportunities
        to divide up the work into smaller independent chunks. It is easier to
        manage several small independent projects than one large project with
        interdependent parts.</p></li><li class="listitem"><p><span class="emphasis"><em>Prioritize</em></span>. The world of business changes
        rapidly. Large projects' requirements change many times before they're
        completed. Important requirements usually remain important as the
        business changes, while others change or even evaporate.
        Prioritization lets you deliver the most important requirements
        first.</p></li><li class="listitem"><p><span class="emphasis"><em>Deliver results as soon as possible</em></span>. Few
        people know what they want before they have it. A famous cartoon shows
        the evolution of a project to build a child's swing based on what the
        customer said and what various roles in the project understood. The
        complicated result only faintly resembles a swing. The last panel,
        titled "What would have worked", shows a simple swing using an old
        tire. When the customer has something to try, the solution may be
        simpler than expected. Building the most important things first gets
        you the most important feedback early, when you need it most.</p></li></ul></div><p>Agile advocates<sup>[<a class="footnote" href="#ftn.CHP-35-FN-2" id="CHP-35-FN-2">3</a>]</sup> exhort us to build "the simplest thing that could possibly
    work". Complex architectures fail far more often than simpler
    architectures. Reducing project scope often results in a simpler
    architecture, and is one of the most effective strategies an architect can
    apply to improve the odds of success.</p><div class="footnotes"><br/><hr/><div class="footnote"><p><sup>[<a class="para" href="#CHP-35-FN-1" id="ftn.CHP-35-FN-1">2</a>] </sup>See <span class="emphasis"><em>The Mythical Man-Month: Essays on Software
            Engineering</em></span>, by Frederick Brooks (Addison-Wesley
            Professional)</p></div><div class="footnote"><p><sup>[<a class="para" href="#CHP-35-FN-2" id="ftn.CHP-35-FN-2">3</a>] </sup>See <span class="emphasis"><em>eXtreme Programming eXplained: Embrace
        Change</em></span>, by Kent Beck (Addison-Wesley Professional).</p></div></div></div></p>

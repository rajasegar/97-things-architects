<p><div class="chapter" title="Chapter 83. It Will Never Look Like That"><div class="titlepage"><div><div><h1 class="title"><a id="it_will_never_look_like_that"/>Chapter 83. It Will Never Look Like That</h1></div></div></div><div class="epigraph"><p>Peter Gillard-Moss is a ThoughtWorker and general memeologist
      living in the U.K. He has worked in IT since 2000 on many projects, from
      public-facing websites in media and e-commerce to rich-client banking
      applications and corporate intranets.</p><div class="attribution"><span>—<span class="attribution">Peter Gillard-Moss</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject83_d1e4346"/><img alt="image with no caption" src="/images/id2719993/OEBPS/httpatomoreillycomsourceoreillyimages251996.png.jpg"/></div></div><p><span class="strong"><strong>IT WILL NEVER LOOK LIKE THAT</strong></span>. It
    is all too easy to fall into the trap of investing large amounts of time
    in a design and being confident that the implementation will come out the
    same. A detailed design can easily fool you into believing you have every
    angle covered. The greater the detail and the more in-depth the research,
    the greater your confidence in it. But it is an illusion: it will never
    look like that.</p><p>The truth is, no matter how in-depth, how well researched, and how
    well thought-out your design, it will never come out looking the same as
    in your head. Something will happen, an external factor may affect the
    design: incorrect information, a limitation, an odd behaviour in someone
    else's code. Or you may have got something wrong: an oversight, an
    incorrect presumption, a subtle concept missed. Or something will change:
    the requirements, the technology, or someone may just find a better
    way™.</p><p>Those minor alterations in the design soon stack up and lots of
    minor alterations soon require that one big one has to be made. Before
    long your original concept is on the floor in pieces, and it's back to the
    drawing board. You decide what you needed was more design and more detail,
    so back you go, and the next architectural vision is clearer, more
    radical, more perfect than the last. But before long the same thing
    happens. Those changes start to appear and shift your design, and
    developers keep shoving in more and more stuff trying their best to work
    around the broken design but just breaking it more, and you end up
    screaming, "Of course it's got bugs; it was never designed to do
    that!"</p><p>Design is a discovery process; as we implement, we discover new
    information that is often impossible to know upfront. By accepting that
    design is an ongoing and empirical process in a forever-changing world, we
    learn that the design process must be flexible and ongoing, too. Clinging
    to your original designs and trying to force them through is only going to
    produce one result, so you need to understand that <span class="emphasis"><em>it will never
    look like that</em></span>.</p></div></p>

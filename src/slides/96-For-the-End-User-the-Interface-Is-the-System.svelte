      entrepreneurship. He is currently working as an architect with Akamai
      Technologies.</p><div class="attribution"><span>—<span class="attribution">Vinayak Hegde</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject96_d1e4926"/><img alt="image with no caption" src="/images/id2728802/OEBPS/httpatomoreillycomsourceoreillyimages252004.png.jpg"/></div></div><p><span class="strong"><strong>THERE ARE TOO MANY GOOD PRODUCTS</strong></span>
    hidden behind bad user-interfaces. The end user will access the system
    through its user interface. If the quality of the user's experience while
    interacting with your product suffers, then his impression of your product
    will suffer, no matter how technologically advanced and path-breaking your
    product might be.<a class="indexterm" id="IDX-CHP-96-0329"/><a class="indexterm" id="IDX-CHP-96-0330"/></p><p>The user interface is an important component of architecture and an
    often-neglected one. The architect should enlist the services of
    specialists such as user experience designers and usability experts. The
    user interaction experts, along with the architect, can drive the
    interface design as well as its coupling with the internal mechanisms.
    Involving user interface experts at an early stage and throughout the
    product development phases ensures that the final product is polished, and
    that the integration of the user interface with the product is seamless.
    The architect should also look at doing user interaction testing while the
    product is still in beta with actual end users, and incorporate their
    feedback into the final product.</p><p>Often the usage of a product changes over time as technology changes
    and features are added. The architect should ensure that user interface
    changes with the architecture reflect the expectations of the
    users.</p><p>User interactions should be one of the goals of the complete product
    architecture. In fact, user interaction should be an integral part of the
    decision-making process for architecture tradeoffs and internal product
    documentation as much as robustness and performance. Changes in user
    interaction design should be captured over time, just like code. This is
    especially true in products where the user interface is written in a
    different programming language than the rest of the product.</p><p>It is the architect's responsibility to make the most common
    interactions not only easy but also rewarding for the end user. Better
    user interfaces lead to happier customers, which helps improve customer
    productivity. If your product helps people become more productive, then it
    will contribute to the business's bottom-line.</p></div></p>

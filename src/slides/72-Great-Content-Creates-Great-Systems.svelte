      Technologies. He has a diverse software programming background with
      knowledge of Basic, C, C++, Perl, Java, JSP, JSF, JavaScript, Erlang,
      Scala, Eiffel, and Ruby languages. His main focus is on enabling Fortune
      Global 500 companies and U.S. government agencies through business
      process-automation solutions.</p><div class="attribution"><span>—<span class="attribution">Zubin Wadia</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject72_d1e3910"/><img alt="image with no caption" src="/images/id2718592/OEBPS/httpatomoreillycomsourceoreillyimages251984.png.jpg"/></div></div><p><span class="strong"><strong>I HAVE SEEN MY FAIR SHARE</strong></span> of
    initiatives focus endlessly on requirements, design, development,
    security, and maintenance, but not on the actual point of the system—the
    data. This is especially true in content-based systems in which the data
    is information delivered as unstructured or semi-structured content. Great
    content means the difference between a system that is hollow and one that
    is relevant.</p><p>Content is king. Content is the network. Content is the interface.
    In an increasingly interconnected world, content quality is rapidly
    becoming the difference between success and failure. FaceBook versus
    Orkut/Google versus Cuil/NetFlix versus BlockbusterOnline...the list is
    endless where battles have been won and lost on the content battlefield.
    One could argue that content-related aspects are not the software
    architect's problem—but I think the next decade will certainly disprove
    that.</p><p>Part of the design process for a new system should be devoted to
    assessing content inventory. Designing an effective domain/object/data
    model is not enough.</p><p>Analyze all available content and assess its value on the following
    criteria:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>Is there enough content available? If not, how do we attain
        critical mass?</p></li><li class="listitem"><p>Is the content fresh enough? If not, how do we improve delivery
        rates?</p></li><li class="listitem"><p>Have all possible content channels been explored? RSS feeds,
        email, and paper forms are all channels.</p></li><li class="listitem"><p>Are there effective input streams built to facilitate the
        continual delivery of this content into the system? It's one thing to
        identify valuable content, but another thing altogether to harvest it
        regularly.</p></li></ul></div><p>Make no mistake, the success of a system depends on its content.
    Spend a healthy part of the design process to assess the value of your
    content. If your findings are less than satisfactory, then that's a red
    flag the stakeholders must be advised about. I have seen many systems that
    fulfill all contractual obligations, meet every requirement, and still
    fail because this fairly obvious aspect was ignored. Great content creates
    great systems.</p></div></p>

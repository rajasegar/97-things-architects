      Professional Services on Homeland Security-related projects. He was
      formerly a software consultant, instructor, and architect for Davalen,
      LLC (<a class="ulink" href="http://www.davalen.com">http://www.davalen.com</a>), a Premier IBM
      Business Partner specializing in WebSphere Portlet Factory, WebSphere
      Portal, and Lotus Domino projects.<a class="indexterm" id="IDX-CHP-21-0092"/></p><div class="attribution"><span>—<span class="attribution">Norman Carnovale</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject21_d1e1175"/><img alt="image with no caption" src="/images/id2706051/OEBPS/httpatomoreillycomsourceoreillyimages251928.png.jpg"/></div></div><p><span class="strong"><strong>FAILED PROJECTS CAN HAPPEN FOR A MULTITUDE
    OF REASONS</strong></span>. One of the most common sources of failure is
    altering the project schedule in midstream without proper planning. This
    kind of failure is avoidable, but it can require major effort on the part
    of multiple people. Adjusting the timeline or increasing resources on a
    project are not normally of concern. Problems start when you are asked to
    do more in the same timeline or when the schedule is shortened without
    reducing the workload.</p><p>The idea that schedules can be shortened in order to reduce cost or
    speed up delivery is a very common misconception. You'll commonly see
    attempts to require overtime or sacrifice "less important scheduled tasks"
    (like unit testing) as a way to reduce delivery dates, or increase
    functionality while keeping the delivery dates as is. Avoid this scenario
    at all costs. Remind those requesting the changes of the following
    facts:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>A rushed design schedule leads to poor design, bad
        documentation, and probable quality assurance or user acceptance
        problems.</p></li><li class="listitem"><p>A rushed coding or delivery schedule has a direct relationship
        to the number of bugs delivered to the users.</p></li><li class="listitem"><p>A rushed test schedule leads to poorly tested code and has a
        direct relationship to the number of testing issues
        encountered.</p></li><li class="listitem"><p>All of the above lead to production issues, which are much more
        expensive to fix.</p></li></ul></div><p>The end result is an increase in cost as opposed to a reduction.
    This is normally why the failures happen.</p><p>As an architect you will one day find yourself in the position of
    having to act quickly to increase the likelihood of success. Speak up
    early. First try to maintain quality by negotiating the originally planned
    timeline. If a shortened schedule is necessary, then try to move
    noncritical functionality to future release(s). Obviously this will take
    good preparation, negotiating skills, and a knack for influencing people.
    Prepare by sharpening your skills in those areas today. You will be glad
    you did.</p></div></p>

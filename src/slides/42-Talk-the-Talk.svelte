<p><div class="chapter" title="Chapter 42. Talk the Talk"><div class="titlepage"><div><div><h1 class="title"><a id="talk_the_talk"/>Chapter 42. Talk the Talk</h1></div></div></div><div class="epigraph"><p>Mark Richards is a director and senior solutions architect at
      Collaborative Consulting, LLC, where he is involved in the architecture
      and design of large-scale service-oriented architectures in J2EE and
      other technologies, primarily in the financial services industry. He has
      been involved in the software industry since 1984, and has significant
      experience in J2EE architecture and development, object-oriented design
      and development, and systems integration.<a class="indexterm" id="IDX-CHP-42-0237"/><a class="indexterm" id="IDX-CHP-42-0238"/></p><div class="attribution"><span>—<span class="attribution">Mark Richards</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject42_d1e2555"/><img alt="image with no caption" src="/images/id2612931/OEBPS/httpatomoreillycomsourceoreillyimages251902.png.jpg"/></div></div><p><span class="strong"><strong>IN ANY PROFESSION</strong></span>, jargon is used
    so that individuals within that profession can effectively communicate
    with one another. Lawyers talk to one another about habeas corpus, voir
    dire, and venire; carpenters talk to one another about butt joints, lap
    joints, and flux; and software architects talk to one another about ROA,
    Two Step View, and Layer Supertype. Wait, what was that?</p><p>It is imperative that software architects, regardless of the
    platform they are working in, have an effective means of communication
    among one another. One of those means of communication is through
    architecture and design patterns. To be an effective software architect
    you must understand the basic architecture and design patterns, recognize
    when those patterns are being used, know when to apply the patterns, and
    be able to communicate to other architects and developers using
    them.<a class="indexterm" id="IDX-CHP-42-0239"/><a class="indexterm" id="IDX-CHP-42-0240"/></p><p>Architecture and design patterns can be classified into four basic
    categories: enterprise architecture patterns, application architecture
    patterns, integration patterns, and design patterns. These categories are
    generally based on the level of scope within the overall architecture.
    Enterprise architecture patterns deal with the high-level architecture,
    whereas design patterns deal with how individual components within the
    architecture are structured and behave.<a class="indexterm" id="IDX-CHP-42-0241"/><a class="indexterm" id="IDX-CHP-42-0242"/><a class="indexterm" id="IDX-CHP-42-0243"/></p><p>Enterprise architecture patterns define the framework for the
    high-level architecture. Some of the more common architecture patterns
    include event-driven architecture (EDA), service-oriented architecture
    (SOA), resource-oriented architecture (ROA), and pipeline
    architecture.<a class="indexterm" id="IDX-CHP-42-0244"/><a class="indexterm" id="IDX-CHP-42-0245"/><a class="indexterm" id="IDX-CHP-42-0246"/><a class="indexterm" id="IDX-CHP-42-0247"/></p><p>Application architecture patterns specify how applications or
    subsystems within the scope of a larger enterprise architecture should be
    designed. Some common pattern catalogs in this category include the
    well-known J2EE design patterns (e.g., Session Façade and Transfer Object)
    and the application architecture patterns described in Martin Fowler's
    book <span class="emphasis"><em>Patterns of Enterprise Application Architecture</em></span>
    (Addison-Wesley Professional).</p><p>Integration patterns are important for designing and communicating
    concepts surrounding the sharing of information and functionality between
    components, applications, and subsystems. Some examples of integration
    patterns include file sharing, remote procedure calls, and numerous
    messaging patterns. You can find these patterns at <a class="ulink" href="http://www.enterpriseintegrationpatterns.com/eaipatterns.html">http://www.enterpriseintegrationpatterns.com/eaipatterns.html</a>.<a class="indexterm" id="IDX-CHP-42-0248"/></p><p>Knowing the basic design patterns as described by the Gang of Four
    book <span class="emphasis"><em>Design Patterns: Elements of Reusable Object-Oriented
    Software</em></span> (Addison-Wesley Professional) is a must for any
    software architect. Although these patterns may appear to be too low-level
    for a software architect, they are part of a standard vocabulary that
    makes for effective communication between architects and
    developers.</p><p>It is also important to be aware of and understand the various
    anti-patterns as well. <span class="emphasis"><em>Anti-patterns</em></span>, a term coined
    by Andrew Koenig, are repeatable processes that produce ineffective
    results. Some of the more well-known anti-patterns include Analysis
    Paralysis, Design By Committee, Mushroom Management, and Death March.
    Knowing these patterns will help you avoid the many pitfalls you will most
    likely experience. You can find a list of the common anti-patterns at
    <a class="ulink" href="http://en.wikipedia.org/wiki/Anti-patterns">http://en.wikipedia.org/wiki/Anti-patterns</a>.<a class="indexterm" id="IDX-CHP-42-0249"/><a class="indexterm" id="IDX-CHP-42-0250"/></p><p>Software architects need the ability to communicate with one another
    in a clear, concise, and effective way. The patterns are there; it is up
    to us as software architects to learn and understand these patterns so we
    can "walk the walk and talk the talk."</p></div></p>

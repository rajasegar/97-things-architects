<p><div class="chapter" title="Chapter 59. Prefer Principles, Axioms, and Analogies to Opinion and Taste"><div class="titlepage"><div><div><h1 class="title"><a id="prefer_principles_comma_axioms_comma_and"/>Chapter 59. Prefer Principles, Axioms, and Analogies to Opinion and
    Taste</h1></div></div></div><div class="epigraph"><p>Michael Harmer has worked in software for 16 years as a developer,
      team leader, architect, principal engineer, and practice manager.</p><div class="attribution"><span>—<span class="attribution">Michael Harmer</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject59_d1e3384"/><img alt="image with no caption" src="/images/id2717002/OEBPS/httpatomoreillycomsourceoreillyimages251966.png.jpg"/></div></div><p><span class="strong"><strong>WHEN CREATING YOUR ARCHITECTURE</strong></span>
    you should explicitly use principles, axioms, and analogies to guide the
    creation. This gives the architecture a number of benefits that are not
    present if you simply create by implicitly leveraging your experience,
    opinions, and tastes.<a class="indexterm" id="IDX-CHP-59-0275"/><a class="indexterm" id="IDX-CHP-59-0276"/><a class="indexterm" id="IDX-CHP-59-0277"/></p><p>Documenting your architecture will be easier. You can start by
    describing the principles that were followed. This is much easier than
    trying to communicate your opinions and experience. The principles will
    then provide a convenient handle for those tasked with understanding and
    implementing the architecture. It will also be invaluable for subsequent
    or inexperienced architects who need to work with the architecture.</p><p>An architecture with clear principles is an architecture that frees
    its architect from reviewing everything and being everywhere. It gives
    architects greater leverage and influence. You will not need to be an
    omniscient workaholic to ensure that others can consistently:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>Implement and adapt the architecture</p></li><li class="listitem"><p>Extend the architecture into related domains</p></li><li class="listitem"><p>Reimplement the architecture using newer technologies</p></li><li class="listitem"><p>Work out the detailed edge cases</p></li></ul></div><p>Disagreements about opinion and taste invariably turn into political
    arguments in which authority is used to win. However, disagreements where
    the foundation principles are clear provide a way for more reasoned
    discussion to occur without issues being personalised. It also allows the
    disagreements to be resolved without reference to the architect at
    all.</p><p>Principles and axioms also give an architecture consistency
    throughout its implementation and across time. Consistency is often a
    problem, especially in large systems that span multiple technologies and
    will exist for many years. Clear architectural principles allow those
    unfamiliar with a particular technology or component to reason about and
    more readily understand the unfamiliar technology.</p></div></p>

<p><div class="chapter" title="Chapter 66. If There Is Only One Solution, Get a Second Opinion"><div class="titlepage"><div><div><h1 class="title"><a id="if_there_is_only_one_solution_comma_get"/>Chapter 66. If There Is Only One Solution, Get a Second Opinion</h1></div></div></div><div class="epigraph"><p>Timothy High is a software architect with more than 15 years'
      experience with web, multitiered client-server, and
      application-integration technologies. He is currently working as a
      software architect for Sakonnet Technologies, a leader in Energy Trading
      and Risk Management (ETRM) software.<a class="indexterm" id="IDX-CHP-66-0283"/></p><div class="attribution"><span>—<span class="attribution">Timothy High</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject66_d1e3640"/><img alt="image with no caption" src="/images/id2715880/OEBPS/httpatomoreillycomsourceoreillyimages251954.png.jpg"/></div></div><p><span class="strong"><strong>YOU'VE PROBABLY HEARD THIS SAID
    BEFORE</strong></span>. If you're an experienced architect, you know it's true:
    if you can only think of one solution to a problem, you're in
    trouble.</p><p>Software architecture is about finding the best possible solution
    for a problem given any number of constraints. It is rarely possible to
    satisfy all requirements and constraints with the first solution that
    comes to mind. Generally, tradeoffs must be made by choosing the solution
    that best satisfies the requirements according to the most critical
    priorities.</p><p>If you only have one solution to the problem at hand, it means that
    you will have no room to negotiate these tradeoffs. It's very possible
    that this one solution will be insatisfactory to the stakeholders of your
    system. It also means that if priorities are shifted due to a changing
    business environment, your system may have no room to adapt to new
    requirements.</p><p>Rarely, if ever, is this situation caused by a real lack of options.
    It is much more likely due to the inexperience of the architect in this
    particular problem domain. If you know this is the case, do yourself a
    favor and ask someone more experienced to give you a hand.</p><p>A more insidious manifestation of this problem is when an
    architecture is designed from habit. An architect can be experienced with
    a single style of architecture (e.g., a three-tier, layered client-server
    system), but not know enough to recognize when that style doesn't fit. If
    you find yourself in the situation where you automatically
    <span class="emphasis"><em>know</em></span> the solution, without having done any comparison
    to other approaches, stop, take a step back, and ask yourself if you can
    think of another way to do it. If you can't, you may be in need of some
    help.</p><p>A friend of mine was once the technical person in charge of a small,
    but growing, Internet startup. As its user base started growing, so did
    the load requirements on its system. Performance was going down the tubes,
    and the company was starting to lose some of its hard-won user
    base.</p><p>So, the boss asked him, "What can we do to improve the
    performance?"</p><p>My friend had the answer: "Buy a bigger machine!"</p><p>"What else can we do?"</p><p>"Umm...as far as I know, that's it."</p><p>My friend was fired on the spot. Of course, the boss was
    right.</p></div></p>

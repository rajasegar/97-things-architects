<p><div class="chapter" title="Chapter 85. Make a Strong Business Case"><div class="titlepage"><div><div><h1 class="title"><a id="make_a_strong_business_case"/>Chapter 85. Make a Strong Business Case</h1></div></div></div><div class="epigraph"><p>Yi Zhou is currently the chief software architect in a well-known
      biotech company, and specializes in designing the software platform for
      medical devices and personalizing disease management. He has nearly 20
      years' experience in all aspects of the software development life cycle,
      and excels in business-technology alignment and strategic planning,
      process improvement, architecture and framework design, team building
      and management, and consulting.<a class="indexterm" id="IDX-CHP-85-0300"/></p><div class="attribution"><span>—<span class="attribution">Yi Zhou</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject85_d1e4404"/><img alt="image with no caption" src="/images/id2719478/OEBPS/httpatomoreillycomsourceoreillyimages251992.png.jpg"/></div></div><p><span class="strong"><strong>AS A SOFTWARE ARCHITECT</strong></span>, have you
    had a hard time getting your architecture project well funded? The
    benefits of software architecture are obvious for architects, but are
    mythical for many stakeholders. Mass psychology tells us that "seeing is
    believing" is the strongest belief for most people. At the early phase of
    the projects, however, there is little to demonstrate to convince
    stakeholders of the value of sound software architecture. It's even more
    challenging in the nonsoftware industries where most stakeholders have
    little software-engineering knowledge.</p><p>Mass psychology also shows that most people believe in "perception
    is reality." Therefore, if you can control how people perceive the
    architectural approach you propose, it's virtually guaranteed that you can
    control how they will react to your proposal. How can you mange
    stakeholders' perceptions? Make a strong business case for your
    architecture. People who have the budget authority to sponsor your ideas
    are almost always business-driven.<a class="indexterm" id="IDX-CHP-85-0301"/></p><p>I have employed the following five steps to generate solid business
    cases to successfully sell my architectural approach many times in my
    career:</p><div class="orderedlist"><ol class="orderedlist"><li class="listitem"><p><span class="emphasis"><em>Establish the value proposition</em></span>. The value
        proposition is your executive summary of why your organization's
        business warrants a particular software architecture. The key for this
        is to compare your architectural approach with existing solutions or
        other alternatives. The focus should be put on its capability to
        increase the productivity and efficiency of the business rather than
        how brilliant the technologies are.<a class="indexterm" id="IDX-CHP-85-0302"/></p></li><li class="listitem"><p><span class="emphasis"><em>Build metrics to quantify</em></span>. The values you
        promise to deliver need to be quantified to a reasonable extent. The
        more you measure, the more you can bolster your case that sound
        architecture will lead to a substantial return. The earlier you
        establish metrics, the better you manage people's perceptions that
        help you sell responsible architecture.<a class="indexterm" id="IDX-CHP-85-0303"/></p></li><li class="listitem"><p><span class="emphasis"><em>Link back to traditional business measures</em></span>.
        It would be ideal if you can translate your technical analysis into
        dollar figures. After all, the only constant parameter in the
        traditional business measures is money. Find business analysts as your
        partners if you are not comfortable with financial work.</p></li><li class="listitem"><p><span class="emphasis"><em>Know where to stop</em></span>. Before you know where
        to stop, you need to prepare a roadmap that captures a vision with
        each milestone on it tied directly to business values. Let the
        stakeholders decide where to stop. If the business value for each
        momentum is significant, you're most likely to get continued
        funding.</p></li><li class="listitem"><p><span class="emphasis"><em>Find the right timing</em></span>. Even if you follow
        the previous four steps to generate a solid business case, you still
        may not be able to sell your ideas if you pick the bad timing. I
        remember one of my proposals did not get approved for a long time
        until another project turned out to be a total failure because of poor
        architectural design. Be smart on timing.</p></li></ol></div></div></p>

<p><div class="chapter" title="Chapter 3. Chances Are, Your Biggest Problem Isn't Technical"><div class="titlepage"><div><div><h1 class="title"><a id="chances_are_comma_your_biggest_problem"/>Chapter 3. Chances Are, Your Biggest Problem Isn't Technical</h1></div></div></div><div class="epigraph"><p>Mark Ramm is BDFL for TurboGears 2, a python enthusiast, and a
      generally crazy dude. He's done nearly every job imaginable from
      software architect to network administrator to lobster-trap thrower and
      biker-bar cleaner. He is passionate about making tools that help
      professional and amateur programmers more productive.</p><div class="attribution"><span>—<span class="attribution">Mark Ramm</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject3_d1e399"/><img alt="image with no caption" src="/images/id2612796/OEBPS/httpatomoreillycomsourceoreillyimages251900.png.jpg"/></div></div><p><span class="strong"><strong>RIGHT NOW SOMEONE'S RUNNING</strong></span> a
    failing project to build a payroll system. Probably more than one
    someone.</p><p>Why? Was it because they chose Ruby over Java, or Python over
    Smalltalk? Or because they decided to use Postgres rather than Oracle? Or
    did they choose Windows when they should have chosen Linux? We've all seen
    technology take the fall for failed projects. But what are the chances
    that the problem was really so difficult to solve that Java wasn't up the
    the task?</p><p>Most projects are built by people, and those people are the
    foundation for success and failure. So, it pays to think about what it
    takes to help make those people successful.</p><p>Equally, there's a good chance that there's someone who you think is
    "just not doing it right" and is undermining the project. In these cases,
    the technology you need to solve your problem is very old and well
    established indeed; in fact, it's possibly the most important technical
    innovation in the history of humanity. What you need is a
    <span class="emphasis"><em>conversation</em></span>.</p><p>Mere familiarity with the conversation as a technology isn't enough.
    Learning to treat people with respect, and learning give them the benefit
    of the doubt, is one of the core skills that turn a smart architect into
    an effective one.</p><p>There's lots more to it than this, but a couple of small tips can
    significantly increase your conversational effectiveness:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>Approach these events as conversations—not as
        confrontations.</p><p>If you assume the best about people and treat this situation as
        an opportunity to ask questions, you definitely learn more, and are
        less likely to put people on the defensive.</p></li><li class="listitem"><p>Approach these conversations only after you've got your attitude
        right.</p><p>If you're angry, frustrated, annoyed, or otherwise flustered,
        it's very likely that the other person will interpret your nonverbals
        as an indication that you're on the attack.</p></li><li class="listitem"><p>Use these as opportunities to set mutually agreed-upon
        goals.</p><p>Instead of telling a developer that they need to be quiet in
        meetings because they never let anybody speak, ask if they can help
        you increase other people's participation. Explain that some people
        are more introverted and need longer silences before they enter into a
        conversation, and ask the developer to help you out by waiting five
        seconds before jumping in.</p></li></ul></div><p>If you start with a shared purpose, treat people "problems" as an
    opportunity to learn, and manage your own emotions, you'll not only become
    more effective, you'll also discover that you learn something every
    time.</p></div></p>

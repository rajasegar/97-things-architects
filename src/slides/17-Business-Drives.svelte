<p><div class="chapter" title="Chapter 17. Business Drives"><div class="titlepage"><div><div><h1 class="title"><a id="business_drives"/>Chapter 17. Business Drives</h1></div></div></div><div class="epigraph"><p>Dave Muirhead is a veteran software craftsman and business
      technologist, and is an owner and principal consultant of Blue River
      Systems Group, LLC (BRSG), a Denver-based lean software development and
      technology strategy consulting firm.</p><div class="attribution"><span>—<span class="attribution">Dave Muirhead</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject17_d1e1028"/><img alt="image with no caption" src="/images/id2705505/OEBPS/httpatomoreillycomsourceoreillyimages251920.png.jpg"/></div></div><p><span class="strong"><strong>IN THE CONTEXT OF BUSINESS
    ENTERPRISE</strong></span> application development, an architect must act as a
    bridge between the business and technology communities of an organization,
    representing and protecting the interests of each party to the other,
    often mediating between the two, but allowing the business to drive. The
    business organization's objectives and operating realities should be the
    light in which an architect leads technology-oriented decision
    making.</p><p>Businesses routinely plan for and articulate a specific, desired
    return on investment (ROI) before undertaking a software development
    initiative. The architect must understand the desired ROI, and by
    implication, the limits of the value of the software initiative to the
    business, so as to avoid making technology decisions that could cause the
    opportunity to be outspent. ROI should serve as a major piece of objective
    context in the give-and-take conversations with the business about the
    value of a feature versus the cost of delivering that feature, and with
    the development team about technical design and implementation. For
    example, the architect must be careful to represent the interests of the
    business to the development team by not agreeing to choose technology that
    has unacceptably costly licensing and support cost implications when the
    software is deployed into testing or production.<a class="indexterm" id="IDX-CHP-17-0086"/></p><p>Part of the challenge of letting the business "drive" is providing
    enough quality information about the ongoing software development effort
    back into the business in order to support good business decision making.
    That's where transparency becomes crucial. The architect, in conjunction
    with development management, must create and nurture the means for
    regular, ongoing information feedback loops. This can be accomplished by a
    variety of lean software development techniques, such as big visible
    charts, continuous integration, and frequent releases of working software
    to the business starting early in the project.</p><p>Software development is fundamentally a design activity, in that it
    involves an ongoing process of decision making until the developed system
    goes into production. It is appropriate for software developers to make
    many decisions, but usually not to make business decisions. However, to
    the extent that the business community fails to fulfill its responsibility
    to provide direction, answer questions, and make business decisions for
    the software development team, it is actually delegating the business
    decision making to software developers. The architect must provide the
    macro-context for this ongoing series of micro-decisions made by
    developers, by communicating and protecting the software architecture and
    business objectives, and must seek to ensure that developers do not make
    business decisions. Technical decision making untethered to the
    commitments, expectations, and realities of the business—as articulated by
    the business community on an ongoing basis—amounts to costly speculation
    and often results in an unjustifiable expenditure of scarce
    resources.</p><p>The long-term interests of the software development team are best
    served when business drives.</p></div></p>

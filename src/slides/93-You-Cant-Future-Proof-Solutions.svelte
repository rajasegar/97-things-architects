        FUTURE</strong></span>. If you accept this as a universal truth, then the
        question becomes, how far ahead is the future? One decade? Two years?
        Twenty minutes? If you can't predict the future, then you can't
        predict anything beyond right now. This very moment and the ones that
        preceded it are all you are know until the next moment occurs. This is
        the reason we have car accidents; if you knew you were going to have
        an accident on Thursday, you would probably stay home.<a class="indexterm" id="IDX-CHP-93-0317"/><a class="indexterm" id="IDX-CHP-93-0320"/><a class="indexterm" id="IDX-CHP-93-0318"/><a class="indexterm" id="IDX-CHP-93-0321"/><a class="indexterm" id="IDX-CHP-93-0319"/><a class="indexterm" id="IDX-CHP-93-0322"/><a class="indexterm" id="IDX-CHP-93-0323"/><a class="indexterm" id="IDX-CHP-93-0324"/></p><p>Yet we see software architects try to design systems that will
        be, for lack of a better term, "future-proof" all the time. It's
        simply not possible to future-proof an architecture. No matter what
        architectural decision you make now, that choice will become obsolete
        eventually. The cool programming language you used will eventually
        become the COBOL of tomorrow. Today's distributed framework will
        become tomorrow's DCOM. In short, today's solution will always be
        tomorrow's problem.</p><p>If you accept this fact—that the choices you make today will
        most certainly be wrong in the future—then it relieves you of the
        burden of trying to future-proof your architectures. If any choice you
        make today will be a bad choice in the future, then don't worry about
        what the future will hold—choose the best solution that meets your
        needs today.</p><p>One of the problems architects have today is analysis paralysis,
        and a big contribution to that problem is trying to guess the best
        technology for the future. Choosing a good technology for right now is
        hard enough; choosing one that will be relevant in the future is
        futile. Look at what your business needs now. Look at what the
        technology market offers now. Choose the best solution that meets your
        needs now, because anything else will not only be wrong choice
        tomorrow, but the wrong choice today.</p></div></div></div></p>

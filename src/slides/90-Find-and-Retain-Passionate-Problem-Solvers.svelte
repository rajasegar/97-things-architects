<p><div class="chapter" title="Chapter 90. Find and Retain Passionate Problem Solvers"><div class="titlepage"><div><div><h1 class="title"><a id="find_and_retain_passionate_problem_solve"/>Chapter 90. Find and Retain Passionate Problem Solvers</h1></div></div></div><div class="epigraph"><p>Chad LaVigne is a solutions architect and technical hired gun for
      Baltimore-based TEKSystems, Inc. He works primarily in the Minneapolis
      area designing and implementing solutions utilizing Enterprise Java
      technologies.<a class="indexterm" id="IDX-CHP-90-0313"/></p><div class="attribution"><span>—<span class="attribution">Chad LaVigne</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject90_d1e4652"/><img alt="image with no caption" src="/images/id2716437/OEBPS/httpatomoreillycomsourceoreillyimages251958.png.jpg"/></div></div><p><span class="strong"><strong>PUTTING TOGETHER A TEAM</strong></span> of
    outstanding developers is one of the most important things you can do to
    ensure the success of a software project. While the concept of keeping
    that team together does not seem to get as much lip service, it is equally
    important. Therefore, you need to carefully select your development team
    and diligently protect it once assembled.</p><p>Most people probably agree that finding top-notch developers
    requires thorough technical interviewing. But what does thorough mean
    exactly? It doesn't mean requiring candidates to answer difficult
    questions about obscure technical details. Screening for specific
    technical knowledge is definitely part of the process but turning an
    interview into a certification test will not guarantee success. You are
    searching for developers with problem-solving skills and passion. The
    tools you use are sure to change; you need people who are good at
    attacking problems regardless of the technologies involved. Proving
    someone has the ability to recite every method in an API tells you very
    little about that person's aptitude or passion for solving
    problems.</p><p>However, asking someone to explain her approach to diagnosing a
    performance problem gives you great insight into her methods for problem
    solving. If you want to learn about a developer's ability to apply lessons
    learned, ask what she would change given the chance to start her most
    recent project anew. Good developers are passionate about their work.
    Asking them about past experience will bring out that passion and tell you
    what correct answers to technical trivia questions cannot.</p><p>If you have been diligent in staffing a strong team, you want to do
    whatever is within your power to keep the team together. Retention factors
    such as compensation may be out of your hands, but make sure you're taking
    care of the little things that help to foster a healthy work environment.
    Good developers are often strongly motivated by recognition. Use this fact
    to your advantage and acknowledge stellar performances. Finding great
    developers is difficult; letting people know they are valued is not. Don't
    miss simple chances to build morale and boost productivity.</p><p>Be careful with negative reinforcement. Too much of it may stifle a
    developer's creativity and reduce productivity. Worse yet, it's likely to
    create dissension within the team. Good developers are smart; they know
    they're not wrong all of the time. If you're picking apart the minutiae of
    their work, you'll lose their respect. Keep criticism constructive and
    don't require that every solution look like it came from you.</p><p>The importance of staffing your development team correctly can't be
    overstated. These are the people who do the heavy lifting. When it comes
    to estimates, they're all treated as equal producers. Make sure it's tough
    to crack the starting lineup, and once you've got a winning team, go the
    extra mile to keep it together.</p></div></p>

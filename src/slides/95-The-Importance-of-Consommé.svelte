<p><div class="chapter" title="Chapter 95. The Importance of Consommé"><div class="titlepage"><div><div><h1 class="title"><a id="the_importance_of_consomme"/>Chapter 95. The Importance of Consommé</h1></div></div></div><div class="epigraph"><p>Eben Hewitt is a principal on the architecture team at a
      multibillion-dollar national retail company, where he is currently
      focused on designing and implementing its service-oriented architecture.
      He is the author of the upcoming <span class="emphasis"><em>Java SOA Cookbook</em></span>
      from O'Reilly.<a class="indexterm" id="IDX-CHP-95-0326"/></p><div class="attribution"><span>—<span class="attribution">Eben Hewitt</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject95_d1e4890"/><img alt="image with no caption" src="/images/id2719601/OEBPS/httpatomoreillycomsourceoreillyimages251994.png.jpg"/></div></div><p><span class="strong"><strong>A CONSOMMÉ IS AN EXTREMELY CLARIFIED
    BROTH</strong></span>, usually made with beef or veal, served as a delicate
    soup. A well-made consommé is perfectly clear. It is considered
    challenging and time-consuming to make, because there is only one way to
    remove the fat and other solids that cloud the broth and gain the absolute
    clarity the dish requires: repeated, simple, fine-grained straining. This
    straining again and again, this hyperconscious refining of the mixture,
    creates an intensely rich flavor. It's as if to taste a consommé is to
    taste the very essence of a thing. That is, in fact, the point of the
    dish.<a class="indexterm" id="IDX-CHP-95-0327"/></p><p>In culinary schools in America, a simple test is administered to
    student chefs making consommé: the teacher drops a dime into your amber
    broth; if you can read the date on the dime resting at the bottom of the
    bowl, you pass. If you can't, you fail.</p><p>Software architecture requires a continual refinement of thought, a
    repeated straining of ideas until we have determined the essence of each
    requirement in the system. We ask, like Hamlet holding Yorick's skull,
    what is this thing? What are its properties? Its relations? We clarify our
    concepts, to make the relations within the architecture verifiably true,
    internally consistent.<a class="indexterm" id="IDX-CHP-95-0328"/></p><p>Many missed requirements and bugs in software can be traced to
    ambiguous, general language. Ask customers, developers, analysts, and
    users the same questions repeatedly, until they are drowsy with boredom.
    Now disguise your question to pose it in a different way, like an attorney
    looking for a snag in the alibi, to tease out anything new, any
    differences or contradictions. Strain and strain again.</p><p>Focus on what can be removed from the concepts presented in the
    architecture, the nouns that compose them, to determine their essence.
    Bring surgical precision to the language you find in your requirements,
    rejecting ambiguity, generality, unwarranted assumptions, or extraneous
    verbiage. This serves to make your concepts richer, more robust. Reduce
    and reduce again.</p><p>Test statements by asking "Would you make the same statement if I
    appended 'always and forever and in every circumstance' to it?" People
    resist committing to absolutes like this, and must refine their words.
    Force representations of concepts through a linguistic sieve to clarify
    them. Do this again, until you are left with only the exhaustive list of
    simple and verifiably true statements that describe the essential nature
    of the system.</p><p>You'll know when the architecture is finished: when you can look
    through it and read the date on a dime.</p></div></p>

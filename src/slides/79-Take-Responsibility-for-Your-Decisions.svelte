<p><div class="chapter" title="Chapter 79. Take Responsibility for Your Decisions"><div class="titlepage"><div><div><h1 class="title"><a id="take_responsibility_for_your_decisions"/>Chapter 79. Take Responsibility for Your Decisions</h1></div></div></div><div class="epigraph"><p>Yi Zhou is currently the chief software architect in a well-known
      biotech company, and specializes in designing the software platform for
      medical devices and personalizing disease management. He has nearly 20
      years' experience in all aspects of the software development life cycle,
      and excels in business-technology alignment and strategic planning,
      process improvement, architecture and framework design, team building
      and management, and consulting.<a class="indexterm" id="IDX-CHP-79-0293"/></p><div class="attribution"><span>—<span class="attribution">Yi Zhou</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject79_d1e4190"/><img alt="image with no caption" src="/images/id2719478/OEBPS/httpatomoreillycomsourceoreillyimages251992.png.jpg"/></div></div><p><span class="strong"><strong>SOFTWARE ARCHITECTS HAVE TO TAKE
    RESPONSIBILITY</strong></span> for their decisions, as they have much more
    influential power in software projects than most people in organizations.
    Studies of software projects show that more than two-thirds of them either
    are outright failures or deliver unsuccessfully (deadline slip, budget
    overruns, or low customer satisfaction). Many of the root causes point to
    improper decisions software architects made, or failures of follow-through
    on the right architectural decisions.</p><p>How can you become a responsible software architect who makes
    effective architectural decisions?</p><p>First, you have to be fully cognizant of your decision process,
    whether it is agile or ceremonial. You should <span class="emphasis"><em>not</em></span>
    claim that an architectural decision has been made until the following two
    conditions are met:</p><div class="itemizedlist"><ul class="itemizedlist"><li class="listitem"><p>A decision has been put in writing because architectural
        decisions are rarely trivial. They must be substantiated and
        traceable.</p></li><li class="listitem"><p>A decision has been communicated to the people who execute it
        and the people who will be affected directly or indirectly.
        Communication is all about creating shared understanding.</p></li></ul></div><p>Second, review your architectural decisions periodically. Examine
    the results of your decisions against expectations. Identify architectural
    decisions that remain valid and those that do not.</p><p>Third, enforce your architectural decisions. Many software projects
    get software architects involved only in the design phase, then they move
    on to other projects or the consultation contract ends. How can they
    ensure that their deliberate architectural decisions have been implemented
    correctly? Their decisions will be at best good intentions unless they
    follow through with them.</p><p>Finally, delegate some decision making to others who are experts in
    a problem domain. Many architects wrongly assume they have to make every
    architectural decision. Therefore, they position themselves as know-it-all
    experts. In reality, there's no such thing as a universal technical
    genius. Architects have areas in which they are quite proficient, areas in
    which they are knowledgeable, and areas in which they are simply
    incompetent. Adept architects delegate decisions about domain problems in
    which they are not proficient.</p></div></p>

<p><div class="chapter" title="Chapter 36. Value Stewardship Over Showmanship"><div class="titlepage"><div><div><h1 class="title"><a id="value_stewardship_over_showmanship"/>Chapter 36. Value Stewardship Over Showmanship</h1></div></div></div><div class="epigraph"><p>Barry Hawkins has played various roles in his 13 years in the
      software industry, from lone developer to team lead to Agile coach and
      mentor. Barry is one of the few native Atlantans, currently specializing
      in coaching and mentoring for Agile software development and
      domain-driven design.<a class="indexterm" id="IDX-CHP-36-0226"/></p><div class="attribution"><span>—<span class="attribution">Barry Hawkins</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject36_d1e2233"/><img alt="image with no caption" src="/images/id2713246/OEBPS/httpatomoreillycomsourceoreillyimages251940.png.jpg"/></div></div><p><span class="strong"><strong>WHEN AN ARCHITECT ENTERS A
    PROJECT</strong></span>, there is an understandable desire to prove his or her
    worth. Being assigned the role of software architect typically indicates
    implicit trust on the part of the company in the architect's technical
    leadership, and it only follows that the architect would desire to make
    good on that expectation as soon as possible. Unfortunately, there are
    those who labor under the misapprehension that proving one's worth
    consists of showmanship—bedazzling if not baffling the team with one's
    technical brilliance.</p><p>Showmanship, the act of appealing to your audience, is important in
    marketing, but it's counterproductive to leading a software development
    project. Architects must win the respect of their team by providing solid
    leadership and by truly understanding the technical and business domain in
    which they are expected to operate.</p><p>Stewardship, taking responsibility and care of another's property,
    is the appropriate role of an architect. An architect must act in the best
    interests of his customer and not pander to the needs of his own
    ego.</p><p>Software architecture is about serving the needs of one's customers,
    typically through direction from those with domain expertise that
    surpasses one's own. Pursuing successful software development will lead
    one to create solutions of compromise, balancing the cost and complexity
    of implementation against the time and effort available to a project. That
    time and effort are the resources of the company, which the software
    architect must steward without self-serving undercurrents. Unduly complex
    systems that sport the latest hot framework or technology buzzword seldom
    do so without some sacrifice at the company's expense. Much like an
    investment broker, the architect is being allowed to play with his
    client's money, based on the premise that his activity will yield an
    acceptable return on investment.</p><p>Value stewardship over showmanship; never forget that you are
    playing with other people's money.</p></div></p>

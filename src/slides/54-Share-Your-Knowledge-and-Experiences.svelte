<p><div class="chapter" title="Chapter 54. Share Your Knowledge and Experiences"><div class="titlepage"><div><div><h1 class="title"><a id="share_your_knowledge_and_experiences"/>Chapter 54. Share Your Knowledge and Experiences</h1></div></div></div><div class="epigraph"><p>Paul W. Homer is a software developer, writer, and occasional
      photographer, who was drawn into software development several decades
      ago and has been struggling ever since with trying to build increasingly
      complex systems.<a class="indexterm" id="IDX-CHP-54-0269"/></p><div class="attribution"><span>—<span class="attribution">Paul W. Homer</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject54_d1e3165"/><img alt="image with no caption" src="/images/id2716310/OEBPS/httpatomoreillycomsourceoreillyimages251956.png.jpg"/></div></div><p><span class="strong"><strong>FROM ALL OF OUR EXPERIENCES</strong></span>,
    including both success and failure, we learn a great deal. In a young
    industry like software development, disseminating this experience and
    knowledge is vital in helping sustain progress. What each team learns in
    its own tiny little corner of the world is possibly influential across the
    globe.</p><p>Realistically our fundamental knowledge base for software
    development—that is, the knowledge that is absolute and theoretically
    correct—is small compared to what is necessary to successfully develop a
    project. To compensate, we guess, rely on intuitive judgments or even pick
    randomly. In that, any major development project generates empirical
    evidence into what works and what fails. We're gradually working through
    the permutations, which we want to apply back to the industry as a
    whole.</p><p>At an individual level, we are all trying to grow and come to
    understand how to build larger and larger systems. The course of our
    careers will take us toward ever-increasing challenges, for which we want
    our past experiences to help guide us. Being there is one thing, but to
    get the most from the experience we often have to rationalize it. The best
    and easiest way of working through it is to attempt to explain it to
    another person.</p><p>The act of discussing something always helps to show its
    weaknesses.You don't really understand something until you can explain it
    easily. It's only by putting forth our explanations and discussing them
    that we solidify the experience into knowledge.</p><p>Another point is that while we may have had specific experiences,
    the inferences we draw from them may not be entirely correct in the
    overall context. We may not have been as successful as we thought, or as
    smart as we wanted. Of course, testing your knowledge against the real
    world is scary, particularly when you find out that something dear is
    myth, incorrect, or was never true; it's hard to be wrong.</p><p>Ultimately, we are human beings so not everything in our minds is
    correct; not every thought we have is reasonable. It's only when we accept
    our flaws that we open up the possibility of improving. The old adage
    about learning more from failure always holds. If our ideas and beliefs do
    not stand the test of a debate, then it is better we find out now than
    build on it later.</p><p>We really want to share our knowledge and experience to help the
    industry progress; we also realize it helps us to understand and correct
    it. Given the state of so much of our software, it is clearly important
    for us to take every opportunity to share the things we know, what we
    think we know, and what we've seen. If we help those around us to improve,
    they'll help us to reach our full potential.</p></div></p>

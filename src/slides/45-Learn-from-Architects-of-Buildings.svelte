<p><div class="chapter" title="Chapter 45. Learn from Architects of Buildings"><div class="titlepage"><div><div><h1 class="title"><a id="learn_from_architects_of_buildings"/>Chapter 45. Learn from Architects of Buildings</h1></div></div></div><div class="epigraph"><p>After many years as an amateur, Keith Braithwaite was first paid
      to write software in 1996. After that first job, maintaining a compiler
      built with lex and yacc, he progressed first to modelling microwave
      propagation for GSM network planning, then seasonal variations in demand
      for air freight, in C++. A move to consultancy (and Java) introduced him
      to CORBA and then EJB, and then what was called at the time
      "e-commerce." He is currently a principal consultant with Zuhlke and
      manages its Centre of Agile Practice.<a class="indexterm" id="IDX-CHP-45-0254"/></p><div class="attribution"><span>—<span class="attribution">Keith Braithwaite</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject45_d1e2736"/><img alt="image with no caption" src="/images/id2698488/OEBPS/httpatomoreillycomsourceoreillyimages251912.png.jpg"/></div></div><div class="blockquote"><blockquote class="blockquote"><p><span class="emphasis"><em>Architecture is a social act and the material theater of
      human activity</em></span>.</p><p>—Spiro Kostof</p></blockquote></div><p><span class="strong"><strong>HOW MANY SOFTWARE ARCHITECTS</strong></span> see
    their role as exclusively, or primarily, technical? Is it not rather that
    they are the conciliators, go-betweens and arbiters of the warring
    factions among the stake-holders? How many approach their work in a purely
    intellectual spirit, without giving proper weight to the human factors of
    their job?</p><div class="blockquote"><blockquote class="blockquote"><p><span class="emphasis"><em>A great architect is not made by way of a brain nearly
      so much as he is made by way of a cultivated, enriched
      heart</em></span>.</p><p>—Frank Lloyd Wright</p></blockquote></div><p>What more strongly marks out the architects in your organization:
    raw intellectual horsepower and vast capacity to recall technical
    minutiae, or taste, refinement, and generosity of spirit? Under which
    tendency would you prefer to work?</p><div class="blockquote"><blockquote class="blockquote"><p><span class="emphasis"><em>A doctor can bury his mistakes but an architect can only
      advise his client to plant vines</em></span>.</p><p>—ibid</p></blockquote></div><p>Is the "maintenance" of "legacy" systems anything more than pruning
    those vines? Would you, as an architect, have the intestinal fortitude to
    scrap a piece of work that had failed? Or would you cover it up? Wright
    also said that the architect's best friend was the sledgehammer. What have
    you demolished recently?</p><div class="blockquote"><blockquote class="blockquote"><p><span class="emphasis"><em>Architects believe that not only do they sit at the
      right hand of God, but that if God ever gets up, they take the
      chair</em></span>.</p><p>—Karen Moyer</p></blockquote></div><p>For "God," read "customer."</p><div class="blockquote"><blockquote class="blockquote"><p><span class="emphasis"><em>In architecture as in all other operative arts, the end
      must direct the operation. The end is to build well. Well building has
      three conditions: Commodity, Firmness and Delight</em></span>.</p><p>—Henry Watton</p></blockquote></div><p>When was the last time you saw a piece of software whose
    architecture gave you any delight? Do you aim to give delight with your
    work?</p><div class="blockquote"><blockquote class="blockquote"><p>
        <span class="emphasis"><em>No person who is not a great sculptor or painter can be
      an architect. If he is not a sculptor or painter, he can only be a
      builder</em></span>
      </p><p>—John Ruskin</p></blockquote></div><p>Does artistry play its proper part in your architecture? Is the
    assemblage of components to make systems informed by a painterly concern
    for shape and texture, a sculptural sense of balance and implied motion,
    or the importance of negative space?</p><p>And finally, no gloss is required on this comment, a sure remedy for
    the software architect's most damaging syndrome.</p><div class="blockquote"><blockquote class="blockquote"><p><span class="emphasis"><em>It seems a fantastic paradox, but it is nevertheless a
      most important truth, that no architecture can be truly noble which is
      not imperfect</em></span>.</p><p>—ibid</p></blockquote></div></div></p>

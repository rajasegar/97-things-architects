<p><div class="chapter" title="Chapter 73. The Business Versus the Angry Architect"><div class="titlepage"><div><div><h1 class="title"><a id="the_business_versus_the_angry_architect"/>Chapter 73. The Business Versus the Angry Architect</h1></div></div></div><div class="epigraph"><p>Chad LaVigne is a solutions architect and technical hired gun for
      Baltimore-based TEKSystems, Inc. He works primarily in the Minneapolis
      area designing and implementing solutions utilizing Enterprise Java
      technologies.<a class="indexterm" id="IDX-CHP-73-0287"/></p><div class="attribution"><span>—<span class="attribution">Chad LaVigne</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject73_d1e3954"/><img alt="image with no caption" src="/images/id2716437/OEBPS/httpatomoreillycomsourceoreillyimages251958.png.jpg"/></div></div><p><span class="strong"><strong>THERE COMES A TIME IN OUR CAREERS</strong></span>
    as architects when we realize many of the issues we encounter are
    recurring. Though the project and industry may change, many of the
    problems are similar. At this point we can draw on our experience to
    provide many solutions quickly, leaving more time to enjoy the challenging
    issues. We're confident in our solutions and we deliver as advertised. We
    have reached homeostasis. This is the perfect time to make a colossal
    mistake—like deciding you know so much that it's time for you to start
    talking more than you listen. This poor decision usually comes with a side
    of cynicism, impatience, and general anger toward inferior minds who dare
    contradict your superior understanding of all things technical and
    otherwise.</p><p>In its worst form this overconfidence bleeds into the business
    realm. This is an excellent way to land your career on a list somewhere
    next to the Black Rhino. The business is our reason for existence. That
    statement probably hurts a little, but we must not lose sight of that
    fact. We live to serve them, not vice versa. Listening to and
    understanding the business that employs us to solve problems is the most
    critical skill we possess. Ever caught yourself impatiently waiting for a
    business analyst to finish talking so you could make your point? Chances
    are, you didn't get his. Show the business domain experts the respect you
    expect to receive; this is the last group of people you want viewing you
    as unapproachable. If they start avoiding you, you're being a catalyst for
    communication breakdown and sabotaging your own project. Remember, when
    you're talking you can only hear something you already know. Don't ever
    start thinking you're so smart that no one else has something valuable to
    say.<a class="indexterm" id="IDX-CHP-73-0288"/></p><p>When we are listening, we'll often disagree with what we hear about
    how the business operates. That's fine. We can make suggestions for
    improvement and should definitely do so. However, if at the end of the day
    you disagree with how the business is run and it's not going to change,
    that's just too bad. Don't allow yourself to become a disgruntled genius
    who spends all of his time trying to impress others by making witty,
    condescending statements about how poorly the company is run. They won't
    be impressed. They've met that guy before and they don't really like him.
    One of the key ingredients to the recipe for a great architect is passion
    for your work, but you don't want too much passion of the angry variety.
    Learn to accept disagreements and move on. If the differences are too
    great and you find yourself continually at odds with the business, find a
    company that's easier for you to get behind and design solutions for them.
    Regardless of how, find a way to establish a good relationship with the
    business and don't let your ego damage it. It will make you a happier,
    more productive architect.</p></div></p>

      source project Parrot. In more than 25 years as a programmer, she has
      developed everything from games to linguistic analysis tools, e-commerce
      websites, shipping fulfillment, compilers, and database replication
      systems; worked as a language designer, project manager, conference
      organizer, editor, and consultant; been president of an open source
      software foundation; written two books; and founded a tech publishing
      company.</p><div class="attribution"><span>—<span class="attribution">Allison Randal</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject11_d1e720"/><img alt="image with no caption" src="/images/id2698614/OEBPS/httpatomoreillycomsourceoreillyimages251914.png.jpg"/></div></div><p><span class="strong"><strong>DESIGN IS A BEAUTIFUL THING</strong></span>. A
    systematic, detailed presentation and review of a problem space and
    solution reveals errors and opportunities for improvement, sometimes in a
    startlingly dramatic way. The specifications are important because they
    provide the pattern for building. Taking the time to think through the
    architecture is important, both on a macro level with an eye for
    interactions between components and, on a micro level with an eye for
    behavior within a component.</p><p>Unfortunately it's far too easy to get wrapped up in the process of
    design, enthralled by architecture in abstract. The fact is that
    specifications alone have no value. The ultimate goal of a software
    project is a production system. A software architect must always keep an
    eye on this goal, and remember that design is merely a means to an end,
    not an end in itself. An architect for a skyscraper who ignored the laws
    of physics to make the building more beautiful would soon regret it.
    Losing sight of the goal of working code spells serious trouble for any
    project.</p><p>Value the team members who work on implementing your vision. Listen
    to them. When they have problems with the design, there's a good chance
    they're right and the design is wrong, or at least unclear. It's your job,
    in these cases, to modify the design to meet real-world constraints by
    working with your team members to determine what works and what does not.
    No design is perfect from the start; all designs need to be modified as
    they are implemented.</p><p>If you're also a developer on the project, value the time you spend
    writing code, and don't believe anyone who tells you it's a distraction
    from your work as architect. Your vision of both macro and micro levels
    will be greatly enhanced by the time you spend in the belly of the beast
    bringing it to life.</p></div></p>

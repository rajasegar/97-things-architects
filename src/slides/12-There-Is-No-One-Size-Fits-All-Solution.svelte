<p><div class="chapter" title="Chapter 12. There Is No One-Size-Fits-All Solution"><div class="titlepage"><div><div><h1 class="title"><a id="there_is_no_one-size-fits-all_solution"/>Chapter 12. There Is No One-Size-Fits-All Solution</h1></div></div></div><div class="epigraph"><p>Randy Stafford is a practicing software professional with 20
      years' experience as a developer, analyst, architect, manager,
      consultant, and author/presenter.<a class="indexterm" id="IDX-CHP-12-0051"/></p><p>Currently for Oracle's middleware development A-Team, he engages
      globally for proof-of-concept projects, architecture reviews, and
      production crises with diverse customer organizations, specializing in
      grid, SOA, performance, HA, and JEE/ORM work.</p><div class="attribution"><span>—<span class="attribution">Randy Stafford</span></span></div></div><div class="informalfigure"><div class="mediaobject"><a id="I_mediaobject12_d1e751"/><img alt="image with no caption" src="/images/id2613014/OEBPS/httpatomoreillycomsourceoreillyimages251904.png.jpg"/></div></div><p><span class="strong"><strong>ARCHITECTS MUST CONTINUOUSLY</strong></span>
    develop and exercise "contextual sense"—because there is no
    one-size-fits-all solution to problems that may be widely
    diverse.<a class="indexterm" id="IDX-CHP-12-0052"/><a class="indexterm" id="IDX-CHP-12-0053"/></p><p>The incisive phrase "contextual sense" was coined, and its meaning
    insightfully described, by Eberhardt Rechtin in his 1991 book
    <span class="emphasis"><em>Systems Architecting: Creating &amp; Building Complex
    Systems</em></span> (Prentice Hall):</p><div class="blockquote"><blockquote class="blockquote"><p>
        <span class="emphasis"><em>[The central ideas of the 'heuristic approach' to
      architecting complex systems] come from asking skilled architects what
      they do when confronted with highly complex problems. The skilled
      architect and designer would most likely answer, 'Just use common
      sense.' ... [A] better expression than 'common sense' is contextual
      sense—a knowledge of what is reasonable within a given context.
      Practicing architects through education, experience, and examples
      accumulate a considerable body of contextual sense by the time they're
      entrusted with solving a system-level problem—typically 10 years."
      (emphasis in the original)</em></span>
        <a class="indexterm" id="IDX-CHP-12-0054"/>
      </p></blockquote></div><p>A big problem in the software industry, in my opinion, is that
    people are often responsible for solving problems requiring more
    contextual sense than they've accumulated. Perhaps this is because the
    software industry is barely two generations old and growing explosively;
    perhaps it will be a sign of maturity in the software industry when this
    problem no longer exists.</p><p>I encounter examples of this problem frequently in my consulting
    engagements. Typical examples include failure to apply domain-driven
    design<sup>[<a class="footnote" href="#ftn.CHP-12-FN-1" id="CHP-12-FN-1">1</a>]</sup> when appropriate, straying from a pragmatic outlook and
    over-designing a software solution for the essential need at hand, and
    making irrelevant or unreasonable suggestions during performance
    optimization crises.<a class="indexterm" id="IDX-CHP-12-0055"/></p><p>The most important knowledge of software patterns is the knowledge
    of when to apply them and when not to apply them, and the same is true of
    different root cause hypotheses and associated corrective actions during
    problem analysis. In both activities—system architecting and problem
    analysis—it is axiomatic that there is no one-size-fits-all solution;
    architects must develop and exercise contextual sense in formulating and
    troubleshooting their architectures.<a class="indexterm" id="IDX-CHP-12-0056"/><a class="indexterm" id="IDX-CHP-12-0057"/></p><div class="footnotes"><br/><hr/><div class="footnote"><p><sup>[<a class="para" href="#CHP-12-FN-1" id="ftn.CHP-12-FN-1">1</a>] </sup>See Eric Evans's <span class="emphasis"><em>Domain-Driven Design: Tackling
        Complexity in the Heart of Software</em></span> (Addison-Wesley
        Professional).</p></div></div></div></p>
